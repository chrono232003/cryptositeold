  <p>
   Washed-up B-list action hero Steven Seagal had to know he was on deadly ground when he
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoiin2gen-ico-leaves-zen-master-steven-seagal-deadly-ground/" rel="noopener" target="_self">
    agreed to promote a scammy bitcoin rip-off called Bitcoiin2Gen
   </a>
   .
  </p>
  <p>
   Two years later, his involvement in the bizarre and
   <a data-wpel-link="internal" href="https://www.ccn.com/steven-seagal-backed-ico-bitcoiin2gen-slapped-cease-desist-order/" rel="noopener" target="_self">
    almost comically shady
   </a>
   project has left him with exit wounds  along with a hefty fine from U.S. securities regulators.
  </p>
  <h2>
   Steven Seagal Fined for Promoting Shady Crypto Project
  </h2>
  <figure aria-describedby="caption-attachment-236101" class="wp-caption aligncenter acss2273f" id="attachment_236101">
   <amp-img alt="steven seagal" class="size-full wp-image-236101 amp-wp-enforced-sizes" height="667" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/steven-seagal-shutterstock.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/02/steven-seagal-shutterstock.jpg 1000w, https://www.ccn.com/wp-content/uploads/2020/02/steven-seagal-shutterstock-300x200.jpg 300w, https://www.ccn.com/wp-content/uploads/2020/02/steven-seagal-shutterstock-768x512.jpg 768w" width="1000">
    <amp-img alt="steven seagal" class="size-full wp-image-236101 amp-wp-enforced-sizes" fallback="" height="667" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg 1000w, https://www.ccn.com/wp-content/uploads/2020/02/steven-seagal-shutterstock-300x200.jpg 300w, https://www.ccn.com/wp-content/uploads/2020/02/steven-seagal-shutterstock-768x512.jpg 768w" width="1000">
    </amp-img>
   </amp-img>
   <figcaption class="wp-caption-text" id="caption-attachment-236101">
    Steven Seagal endorsed the Bitcoiin2Gen ICO and served as Brand Ambassador for the project. Even he may have been bilked by the shady cryptocurrency. | Source: Markus Wissmann/Shutterstock.com
   </figcaption>
  </figure>
  <p>
   The Securities and Exchange Commission announced today that Seagal  the Zen Master and Brand Ambassador for Bitcoiin2Gen  had
   <a data-wpel-link="exclude" href="https://www.sec.gov/news/press-release/2020-42" rel="follow noopener" target="_self">
    agreed to pay $314,000 in disgorgement and penalties
   </a>
   for encouraging investors to purchase the cryptocurrency without disclosing that he was a paid promoter. Hes also barred from promoting securities for three years.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">

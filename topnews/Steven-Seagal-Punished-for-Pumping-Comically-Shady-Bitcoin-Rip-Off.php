    </div>
   </amp-ad>
  </div>
  <figure aria-describedby="caption-attachment-236096" class="wp-caption aligncenter acss2676c" id="attachment_236096">
   <amp-img alt="steven seagal, bitcoin" class="size-full wp-image-236096 amp-wp-enforced-sizes" height="289" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-pyramid.jpg" width="300">
    <amp-img alt="steven seagal, bitcoin" class="size-full wp-image-236096 amp-wp-enforced-sizes" fallback="" height="289" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg" width="300">
    </amp-img>
   </amp-img>
   <figcaption class="wp-caption-text" id="caption-attachment-236096">
    Steven Seagal promoted a bitcoin rip-off that marketed its referral program using a graphic that looked curiously like a pyramid. | Source: Bitcoiin2Gen
   </figcaption>
  </figure>
  <p>
   It would be hilarious if it not for the fact that
   <a data-wpel-link="internal" href="https://www.ccn.com/fbi-victims-2-5-billion-crypto-scam-bitconnect/" rel="noopener" target="_self">
    projects structured almost exactly like this one stole
    <em>
     billions
    </em>
    of dollars
   </a>
   , leaving
   <a data-wpel-link="exclude" href="https://metro.co.uk/2018/01/19/heartbroken-families-lose-life-savings-catastrophic-crash-bitconnect-cryptocurrency-7242067/" rel="follow noopener" target="_self">
    many victims penniless
   </a>
   .
  </p>
  <p>
   And far too often, celebrities like Steven Seagal and Floyd Mayweather
   <a data-wpel-link="internal" href="https://www.ccn.com/tko-floyd-mayweather-dj-khaled-knocked-out-by-sec-probe-into-ico-promos/" rel="noopener" target="_self">
    only made the infestation worse
   </a>
   by promoting these projects with no apparent regard for the investors inevitably left holding the bag.
  </p>
  <h2>
   Seagal May Have Been Hoodwinked Too
  </h2>
  <p>
   As for Bitcoiin2Gen, its essentially dead. But its still
   <a data-wpel-link="external" href="https://coinmarketcap.com/currencies/bitcoiin/markets/reported/" rel="nofollow external noopener noreferrer" target="_self">
    listed on at leasttwo cryptocurrency exchanges
   </a>
   .
  </p>
  <p>
   Its hard not to revel in the irony that it seems Seagal himself may have been hoodwinked too.
  </p>
  <p>
   According to the SEC, he only received $157,000 for his services, far less than the $1 million ($250,000 in cash and $750,000 in cryptocurrency) he had been promised.
  </p>

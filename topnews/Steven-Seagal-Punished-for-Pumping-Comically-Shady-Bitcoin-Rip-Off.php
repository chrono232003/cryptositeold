    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   Its safe to say
   <a data-wpel-link="internal" href="https://www.ccn.com/steven-seagal-backed-ico-bitcoiin2gen-slapped-cease-desist-order/" rel="noopener" target="_self">
    everyone except Steven Seagal saw this coming
   </a>
   .
  </p>
  <p>
   Praised by supporters as a revolutionary way to decentralize capital investment, the initial coin offering boom that accompanied the 2018 bitcoin bust was
   <a data-wpel-link="internal" href="https://www.ccn.com/onecoin-the-crypto-scam-that-refuses-to-die-resurfaces-on-youtube/" rel="noopener" target="_self">
    dominated by hordes of scam artists
   </a>
   who preyed on gullible investors searching for financial freedom in all the wrong places.
  </p>
  <figure aria-describedby="caption-attachment-236103" class="wp-caption aligncenter acss06fd0" id="attachment_236103">
   <amp-img alt="bitcoin rip-off bitcoiin2gen" class="size-full wp-image-236103 amp-wp-enforced-sizes" height="619" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart.jpg 1167w, https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart-300x159.jpg 300w, https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart-1024x543.jpg 1024w, https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart-768x407.jpg 768w" width="1167">
    <amp-img alt="bitcoin rip-off bitcoiin2gen" class="size-full wp-image-236103 amp-wp-enforced-sizes" fallback="" height="619" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg 1167w, https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart-300x159.jpg 300w, https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart-1024x543.jpg 1024w, https://www.ccn.com/wp-content/uploads/2020/02/bitcoiin2gen-chart-768x407.jpg 768w" width="1167">
    </amp-img>
   </amp-img>
   <figcaption class="wp-caption-text" id="caption-attachment-236103">
    Investors who paid $5.00 for Bitcoiin2Gen tokens during the coins ICO purchased an essentially worthless asset. | Source:
    <a data-wpel-link="external" href="https://coinmarketcap.com/currencies/bitcoiin/" rel="nofollow external noopener noreferrer" target="_self">
     CoinMarketCap
    </a>
   </figcaption>
  </figure>
  <p>
   Bitcoiin2Gen was the poster-child for the sleazier side of this fundraising model. From its deliberate misspelling of bitcoin to a
   <a data-wpel-link="internal" href="https://www.ccn.com/exit-wounds-steven-seagal-founders-abandon-bitcoiin-as-ico-ends/" rel="noopener" target="_self">
    press release gleefully announcing its founders exit
   </a>
   from the project, it almost seemed like a self-parody of the ICO hype-machine.
  </p>
  <p>
   And a heavy-handed one at that.
  </p>
  <p>
   The projects marketing materials told investors their cryptocurrency would surge by more than 7,500% in value in less than a year and even advertised its
   <del>
    referral program
   </del>
   pyramid scheme using a literal pyramid graphic:
  </p>

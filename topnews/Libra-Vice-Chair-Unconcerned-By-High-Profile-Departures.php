   <p>
    <span style="color: #000000;">
     Dante Disparte, Vice-Chair of the Libra Association, said that he was unconcerned by recent high-profile departures from the project just two weeks after Vodafone became the latest name to ditch the proposed digital coin.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     <a href="https://www.coindesk.com/libra-head-not-worried-about-the-leavers" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      Speaking to CoinDesk
     </a>
     at the World Economic Forum in Davos last week, Disparte said that the Libra project was picking up pace despite huge regulatory resistance to the project and the departure of several of the associations initial supporters, namely Mastercard, Visa, and Vodafone. He also said that the door remains open for those firms to return to the project in the future, as well as praising the open-source nature of Libra as a means for companies who are not part of the association to still participate in developing Libra.
    </span>

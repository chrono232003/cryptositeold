     </a>
     , bringing the total number of early backers to withdraw from the project to eight. However, while firms such as Visa and MasterCard quit the project over regulatory concerns, Vodafone said it instead intends to focus on developing M-Pesa, its well-established digital payment; the company is looking into expanding beyond the six African nations currently served.

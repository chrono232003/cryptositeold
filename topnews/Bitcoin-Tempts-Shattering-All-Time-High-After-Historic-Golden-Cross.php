  <h2>
   Golden Cross Indicates a Strong Bull Run on the Horizon
  </h2>
  <p>
   The golden cross is a reliable technical indicator signaling a strong bull rally. This happens when the 50-DMA crosses above the 200-DMA.
  </p>
  <p>
   Since 2012, the golden cross only happened three times for bitcoin. The first one was in October 2015. The cross marked the beginning of a strong bull market that saw the top cryptocurrency meteorically rise from $300 to nearly $20,000. Thats a massive gain of over 6,560% in two years.
  </p>
  <p>
   The second time it happened,
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-price-races-toward-wildly-bullish-golden-cross/" rel="noopener" target="_self">
    bitcoin soared from $5,000 in April 2019
   </a>
   to nearly $14,000 in June 2019. Thats a 180% gain in less than 60 days.
  </p>

   History appears to be on the side of bitcoin. If thats truly the case, the $20,000 resistance will be history.
  </p>
  <h2>
   The Bear Case: Bitcoin Trades in a Wide Range Without Posting New High
  </h2>
  <p>
   Even with all the signals pointing to another strong year for bitcoin, its also possible that the cryptocurrency trades in a wide range for most of 2020.
  </p>
  <p>
   Looking at the chart of BTC/USD, theres a lot of real-estate between $12,000 and $19,000. While this suggests a lack of resistance, it could also indicate the absence of a base. Without a solid base, it would be difficult for BTC to sustain its ascent.
  </p>

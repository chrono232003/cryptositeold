   <a data-wpel-link="external" href="https://www.buybitcoinworldwide.com/how-many-bitcoins-are-there/" rel="nofollow external noopener noreferrer" target="_self">
    1,800 BTC to 900 BTC per day
   </a>
   . With supply dwindling as bulls gain momentum, its not difficult to imagine that bitcoins value may skyrocket.
  </p>
  <h2>
   Bitcoin Tends to Soar After Climbing Above the 200-Day Moving Average
  </h2>
  <p>
   Many retail investors are focused on day-to-day price fluctuations that they tend to overlook bigger developments. On Jan. 28, bitcoin broke above the 200-day moving average (DMA). To many technical analysts, this is a strong signal that the trend has shifted from bearish to bullish. However, it goes way beyond that.
  </p>

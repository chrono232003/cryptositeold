   Its quite possible for bitcoin to just trade between $12,000 and $19,000 for the majority of 2020. This would be a tall order for the bears considering that bitcoin tends to get media attention when it is surging. Nevertheless, they have a window to slow bitcoins ascent and keep the top cryptocurrency from printing new highs this year.

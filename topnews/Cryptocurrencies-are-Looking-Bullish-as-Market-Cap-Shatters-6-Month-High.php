  <h2>
   Assessing the Catalysts
  </h2>
  <p>
   There were no immediate catalysts behind the recent rally or for cryptos sudden resurgence this year. Some analysts have speculated that
   <a data-wpel-link="internal" href="https://www.ccn.com/safe-haven-bitcoin-rockets-toward-3-month-high-up-32-ytd/" rel="noopener" target="_self">
    bitcoin is emerging as a viable safe haven
   </a>
   for investors seeking refuge against economic risks, geopolitical uncertainty and the worsening coronavirus epidemic.
  </p>
  <p>
   Theres some evidence to support this.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad2">

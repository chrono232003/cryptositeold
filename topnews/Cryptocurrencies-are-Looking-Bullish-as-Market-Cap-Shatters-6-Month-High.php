  <h2>
   Cryptocurrencies Ascend
  </h2>
  <p>
   Most of the top cryptocurrencies in circulation printed fresh gains on Thursday. Bitcoin peaked at $9,856.43 on Bitstamp, the highest since September. The
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-beats-gold-in-safe-haven-battle-coronavirus-death-toll-rises/" rel="noopener" target="_self">

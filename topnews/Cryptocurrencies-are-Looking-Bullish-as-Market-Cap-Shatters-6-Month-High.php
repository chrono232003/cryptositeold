  <p>
   Bitcoin and gold have shown positive correlation through the first six weeks of the year. Despite vastly under-performing bitcoin,
   <a data-wpel-link="internal" href="https://www.ccn.com/gold-price-poised-knock-out-another-7-year-high-3-reasons-why/" rel="noopener" target="_self">
    gold peaked near seven-year highs
   </a>
   last month as the price rocketed past $1,600.
  </p>
  <p>
   While bitcoin is clearly enjoying a technical tailwind after clawing back above $9,000, altcoins seem to be benefiting from an extremely oversold market. Alts got pummeled in the second half of last year, basically giving back all their earlier gains.
  </p>

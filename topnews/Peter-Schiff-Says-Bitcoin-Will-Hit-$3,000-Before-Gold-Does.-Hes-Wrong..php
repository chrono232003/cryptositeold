  <h2>
   Schiff Stirs The Pot Again
  </h2>
  <p>
   <amp-youtube data-videoid="e3V4pBS5wLA" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   You could say the founder, CEO, and chief global strategist of Euro Pacific Capital is a Wall Street and U.S. dollar perma-bear. His early study of the controversial
   <a data-wpel-link="exclude" href="https://en.wikipedia.org/wiki/Austrian_School" rel="follow noopener" target="_self">
    Austrian school of economics
   </a>
   led him to question the U.S. financial system.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="1136877805" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   Schiff believes that central banking is unsustainable.
  </p>
  <p>
   He thinks
   <a data-wpel-link="exclude" href="https://www.cnbc.com/id/100453993" rel="follow noopener" target="_self">
    central banks like the Federal Reserve cannot resist expanding the money supply
   </a>
   until the value of their currency crashes. It has happened many times in the history of banking. (
   <a data-wpel-link="exclude" href="https://www.investopedia.com/articles/personal-finance/122915/worst-hyperinflations-history.asp" rel="follow noopener" target="_self">
    But usually to unstable, corrupt, and war torn countries
   </a>
   .)
  </p>
  <p>
   So he started Euro Pacific to help investors buy non-dollar denominated securities. Over the years his main focus has been on gold and foreign stocks.
  </p>
  <p>
   <a data-wpel-link="internal" href="https://www.ccn.com/record-u-s-economy-running-massive-recession-deficit/" rel="noopener" target="_self">
    He predicted the Federal Reserves low interest rates were creating a bubble
   </a>
   that would lead to a housing market crash. That was between 2006 and 2007. While he made his dire forecast, mainstream financial analysts laughed.
  </p>
  <p>
   After his prediction came true and the entire global economy ground to a halt,
   <a data-wpel-link="exclude" href="https://www.fool.com/investing/2016/06/16/peter-schiff-loves-gold-to-the-surprise-of-exactly.aspx" rel="follow noopener" target="_self">
    Peter Schiffs net worth rocketed to an estimated $70 million
   </a>
   . Youd think Schiff would love bitcoin and its anti-central banking ethos. But he remains an inveterate gold bug.
  </p>
  <h2>
   Why Schiff Is Wrong About Bitcoin
  </h2>
  <p>
   <amp-youtube data-videoid="h1GzqhSC8OI" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad2">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="6634710517" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   Peter Schiff is wrong about bitcoin for the same reason he was right about the housing market crash. Thats what makes his views on bitcoin fascinating.
  </p>
  <p>
   The Federal Reserves expansionary monetary policy steadily drives down the value of the dollar. This is by design, because it gets people to spend and invest their money instead of hanging onto it. Mainstream economists from John Maynard Keynes to Milton Friedman are proponents of this design, believing it spurs economic growth.
  </p>
  <p>
   The drawback, as Schiff and
   <a data-wpel-link="exclude" href="https://en.wikipedia.org/wiki/Friedrich_Hayek" rel="follow noopener" target="_self">
    the Austrian economists he studied have pointed out
   </a>
   , is the boom and bust economic cycle. As money expands, the rush to spend it creates misallocation of capital to high risk, unproductive investments and speculative bubbles. When the market readjusts prices to reflect the true value of the economy, theres a crash.
  </p>
  <p>
   Bitcoin didnt exist when the housing market crashed and the Great Recession dragged the entire world economy into the doldrums. But if it had, one could imagine how much capital would have fled to it as a global macro hedge. Bitcoin has already proven itself as a safe haven asset like gold. Like gold,
   <a data-wpel-link="exclude" href="https://www.nasdaq.com/articles/bitcoin-was-uncorrelated-with-major-assets-experienced-low-volatility-in-2019-2020-01-07" rel="follow noopener" target="_self">
    the bitcoin price is uncorrelated to equities
   </a>
   .
  </p>
  <p>
   Peter Schiffs question was obviously a tongue-in-cheek wisecrack. But the real question is what will happen first: $3,000 spot price of gold, or bitcoin reaches its previous all time high of $20,000? Lets hope its the latter. Because if gold roughly doubles in price over the next couple years, it means were having another Great Recession.
  </p>

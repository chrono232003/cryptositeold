  <p>
   <a data-wpel-link="internal" href="https://www.ccn.com/10000-bitcoin-looks-imminent-based-on-this-one-crucial-chart/" rel="noopener" target="_self">
    Bitcoins return to $10,000
   </a>
   seemed imminent last week after the largest cryptocurrency achieved an important technical milestone. On Jan. 31, the 50-day exponential moving average (EMA) crossed the 200-day EMA, setting the stage for a continuation of the bullish trend. Since the so-called golden cross was attained, bitcoin has rallied more than $2,000.
  </p>
  <h2>
   Altcoins Surge
  </h2>
  <p>
   Beyond the number one cryptocurrency, altcoins and tokens saw significant upside on Wednesday.
  </p>
  <p>
   Ethereum (ETH) spiked 13.5% to $254.35, its highest since July. XRP rose 8.7% to $0.2969. Bitcoin cash (BCH), EOS, Litecoin (LTC) and Cardano (ADA) rose between 4% and 11%.
  </p>
  <p>
   No other cryptocurrency exemplified alt season like Tezos (XTZ). The tokenization platform spiked more than 20% on Wednesday en route to a new all-time high.
  </p>

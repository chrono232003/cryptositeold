   <a data-wpel-link="external" href="https://www.bitmex.com/app/seriesGuide/XRP" rel="nofollow external noopener noreferrer" target="_self">
    XRP/USD Perpetual Quanto Swap Contract
   </a>
   , lost more than half of its value in a blink of an eye. The asset nosedived from $0.32 to $0.14 in one five-minute candle.
  </p>
  <p>
   The frantic downswing liquidated countless traders. Some of them took to Twitter to express their disgust and frustration over the crypto derivative trading platform. One trader even claimed that his stop loss failed to trigger, thereby liquidating all his crypto holdings. Bitmex wont be compensating him for his losses.
  </p>
  <h2>
   Entire Accounts Got Wiped Out As XRP Plunged
  </h2>
  <p>
   When trading derivatives, such as the XRP/USD Perpetual Quanto Swap Contract, youre trading on leverage. This means your earnings are multiplied if the trade works in your favor. On the other hand, you can lose all of your holdings quickly if the trade goes against you.
  </p>

  <p>
   While sudden swings in bitcoins price are not unusual, the recent selloff undermines the narrative that BTC is becoming a digital safe haven.
  </p>
  <p>
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-crashes-along-with-u-s-stocks-exposing-haven-myth/" rel="noopener" target="_self">
    Bitcoin has been plunging alongside stocks
   </a>
   ever since investors began pricing in the impact of coronavirus. To the naked eye, it appears that bitcoin is moving in lockstep with equities, making it a risk-on asset as opposed to a digital haven.
  </p>
  <p>
   To be clear, bitcoin doesnt usually move in the same direction as stocks. Its not exactly correlated with gold, either. But its inability to attract bids in these extremely volatile times means investors arent hedging their bets with bitcoin  not even close.
  </p>

  <p>
   Schiff believes bitcoin stands no chance of ever displacing gold as the worlds preferred safe-haven.
   <a data-wpel-link="exclude" href="https://www.bloomberg.com/quote/GC1:COM" rel="follow noopener" target="_self">
    Gold futures peaked at $1,704.30 a troy ounce
   </a>
   on Monday, the highest in around seven years.
  </p>
  <p>
   For the time being, investors appear to be fleeing risk assets as coronavirus ravages the global economy. The latest stock-market selloff was stoked by Saudi Arabias decision to its slash forward crude prices after failing to reach a new supply agreement with Russia.
   <a data-wpel-link="internal" href="https://www.ccn.com/oil-prices-hang-in-balance-unlikely-alliance-rattles-middle-east/" rel="noopener" target="_self">
    Global energy producers are at odds about how to re-balance the market
   </a>
   in the wake of the coronavirus shock.
  </p>
  <p>
   The resulting crash in oil prices
   <a data-wpel-link="internal" href="https://www.ccn.com/oil-stocks-collapse-as-goldman-sachs-warns-of-20-crude/" rel="noopener" target="_self">
    triggered a massive slump in energy stocks
   </a>
   , causing the entire market to buckle.
  </p>
  <p>
   Bitcoin is being dragged along for the ride, putting it at risk of further losses.

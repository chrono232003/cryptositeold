  <p>
   After rallying to five-month highs on Friday, bitcoins eventual return to $10,000 and beyond appears highly likely.
  </p>
  <p>
   The bullish bias has a lot to do with the technical charts after bitcoin achieved an important milestone last week.
  </p>
  <h2>
   Bitcoins Golden Cross
  </h2>
  <p>
   The
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-to-repeat-2017-bull-run-based-on-this-adoption-key-analyst/" rel="noopener" target="_self">
    bitcoin price has been gravitating towards $10,000
   </a>
   for the better part of a week now. On Friday, the number one cryptocurrency peaked at $9,871.30 on Gemini, the highest since September. It has since consolidated below $9,800, enough for a 5% weekly gain.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="1136877805" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   At current values, bitcoin has a total market capitalization of $178 billion. Thats an increase of nearly $50 billion since the start of 2020.
  </p>
  <p>
   The sustained upward pressure allowed bitcoin to complete a golden cross formation on the daily chart  one of the most bullish signals in all of trading. A golden cross is when the short-term moving average (in this case, the 50-day exponential moving average) moves above the long-term moving average (such as the 200-day EMA).
  </p>
  <p>
   As the following chart illustrates, this key formation was completed Jan. 31 when BTC pushed north of $8,200. After a minor correction, bitcoin printed a long green candle followed by another smaller one.
  </p>

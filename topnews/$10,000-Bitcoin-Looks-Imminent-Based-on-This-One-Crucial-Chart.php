  <h2>
   Altcoin Market Strengthens
  </h2>
  <p>
   Bitcoin is the undisputed leader in the cryptocurrency space, accounting for some 64% of the overall market. This oversized influence usually means altcoins and tokens follow bitcoins lead.
  </p>
  <p>
   But alts have been slowly decoupling from BTC over the past few weeks, as evidenced by explosive (and seemingly independent) rallies for the likes of
   <a data-wpel-link="internal" href="https://www.ccn.com/icon-icx-goes-parabolic-as-price-triples-for-no-apparent-reason/" rel="noopener" target="_self">
    ICON (ICX)
   </a>
   , Ethereum (ETH),
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-cash-bch-price-surges-14-as-halvening-nears/" rel="noopener" target="_self">
    bitcoin cash (BCH)
   </a>
   and
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-sv-doubles-in-24-hours-skeptics-cry-exit-scam/" rel="noopener" target="_self">
    bitcoin SV (BSV)
   </a>
   .
  </p>

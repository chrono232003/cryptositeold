  <h2>
   Bitcoin-Gold Correlation
  </h2>
  <p>
   Unlike altcoins, bitcoin seems to be convincing more people that its a reliable store of value. Thats because BTC and gold  the markets oldest haven asset  have shown
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-beats-gold-in-safe-haven-battle-coronavirus-death-toll-rises/" rel="noopener" target="_self">
    strong correlation in recent months
   </a>
   .
  </p>
  <p>
   Last month,
   <a data-wpel-link="exclude" href="https://static1.squarespace.com/static/5df0fdb6a73a4b1d59c74702/t/5e1860603ff49c5358cccd9a/1578655848218/Weekly+update+-+Arcane+Research+%28Week+2%29.pdf" rel="follow noopener" target="_self">
    Arcane Research noted
   </a>
   that bitcoins correlation with gold was approaching four-year highs, or levels not seen since 2016.
  </p>
  <p>
   At the time, the two assets were moving in lockstep with one another over
   <a data-wpel-link="internal" href="https://www.ccn.com/iranian-conflict-successful-beta-test-bitcoin-safe-haven-asset/" rel="noopener" target="_self">
    escalating tensions between the United States and Iran
   </a>
   .
  </p>
  <p>
   The two countries have avoided direct conflict, but gold and bitcoin are still trading in the same direction.
  </p>

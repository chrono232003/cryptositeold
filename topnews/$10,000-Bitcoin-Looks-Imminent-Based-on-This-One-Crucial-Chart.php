  <p>
   While its still too early to declare bitcoin digital gold, the two assets share many striking similarities. Whether those similarities resonate with hedge funds, portfolio managers and other traditional investors is yet to be seen. In the meantime, bitcoin still enjoys support from digital natives, early adopters and tech enthusiasts aligned with bitcoins philosophical principles.

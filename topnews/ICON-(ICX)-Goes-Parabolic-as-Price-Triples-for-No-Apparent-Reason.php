   Suddenly and without warning, cryptocurrency ICON (ICX) has been on an absolute tear over the past five days, culminating in a fresh 52-week high that has some traders excited about
   <a data-wpel-link="internal" href="https://www.ccn.com/crypto-investor-declares-alt-season-litecoin-ethereum-eos-come-alive/" rel="noopener" target="_self">

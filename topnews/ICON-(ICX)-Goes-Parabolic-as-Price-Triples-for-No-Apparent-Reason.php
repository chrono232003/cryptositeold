  <h2>
   ICX Surges
  </h2>
  <p>
   ICX catapulted more than 70% on Tuesday and peaked at $0.6031, the highest since August 2018. From last weeks trough to Tuesdays peak, ICXs price had more than tripled.
  </p>
  <p>
   At the time of writing, the coin was hovering at $0.4838, booking gains of nearly 32% for the day. ICX has gained a staggering 326% since the start of 2020. By comparison,
   <a data-wpel-link="internal" href="https://www.ccn.com/safe-haven-bitcoin-rockets-toward-3-month-high-up-32-ytd/" rel="noopener" target="_self">
    bitcoin has rallied about 28%
   </a>
   .
  </p>

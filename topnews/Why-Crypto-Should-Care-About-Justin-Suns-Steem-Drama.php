 <div class="article-pharagraph" id="node-33">
  <p class="text">
   The prior group of witnesses has begun signalling for an alternate software, 22.4444, in protest. The multiple fours are meant to have negative connotations, as "4" is not seen as a lucky number in China. Users have also been posting giant images of the numeral 4 in threads on Steemit.
  </p>
 </div>
 <div class="article-pharagraph" id="node-34">
  <p class="text">
   The community could still move to hard fork. If they did so, no doubt Steemit would continue to point to the current chain, 22.5, now controlled by Tron allies, but others might not. There are hundreds of apps built on Steem, including alternative blogging front-ends that could start reading a new chain rather than the one controlled by Tron.
  </p>
 </div>
 <div class="article-pharagraph" id="node-35">
  <p class="text">
   Blogging alternatives on Steem include
   <a data-reactroot="" href="https://esteem.app/" rel="noopener noreferrer" target="_blank">
    eSteem
   </a>
   ,
   <a data-reactroot="" href="https://busy.org/" rel="noopener noreferrer" target="_blank">
    Busy.org
   </a>
   and
   <a data-reactroot="" href="https://steempeak.com/" rel="noopener noreferrer" target="_blank">
    SteemPeak
   </a>
   , according to Stokes. These could all opt to redirect themselves to look at a new chain if the dispute goes far enough.
  </p>
 </div>
 <div class="article-pharagraph" id="node-36">
  <p class="text">
   On balance, Lanparty said, leaders are keeping apps live in order to not silence the community. However, he pulled an app he had built,
   <a data-reactroot="" href="https://steemwallet.app/" rel="noopener noreferrer" target="_blank">
    SteemWallet
   </a>
   , from the iOS and Android app stores in protest.
  </p>
 </div>
 <div class="article-heading" id="node-37">
  <h4>
   <strong>
    Exchange culpability
   </strong>
  </h4>
 </div>
 <div class="article-pharagraph" id="node-38">
  <p class="text">
   "This is a larger discussion and potentially historical moment for blockchains," Stokes told CoinDesk. "The mechanisms we use to protect these byzantine-fault-tolerant systems, how secure are they really? This is being tested right now."
  </p>
 </div>
 <div class="article-pharagraph" id="node-39">
  <p class="text">
   Somewhat ironically, Lanparty said part of the reason why it soft-forked rather than hard-forked was out of deference to the exchanges. A hard fork would have required exchanges to run new code so their users could withdraw tokens, and the witnesses making the change felt this would be unfair to users.
  </p>
 </div>
 <div class="article-pharagraph" id="node-40">
  <p class="text">
   However, until the Steem software is changed, tokens will be locked up for much longer. Once tokens are locked up to vote, it is designed to take 13 weeks to "power down" (in Steem parlance) and make them exchangeable again. (Of course, the new slate of witnesses now have the power to simply shorten this window.)
  </p>
 </div>
 <div class="article-pharagraph" id="node-41">
  <p class="text">
   One user told CoinDesk he initiated a withdrawal of 500,000 STEEM on Binance following last night's news and it took three hours to arrive, shortly following the Binance
   <a data-reactroot="" href="https://twitter.com/cz_binance/status/1234574737768095746" rel="noopener noreferrer" target="_blank">
    CEO's announcement
   </a>
   that it will "likely remove the vote."
  </p>

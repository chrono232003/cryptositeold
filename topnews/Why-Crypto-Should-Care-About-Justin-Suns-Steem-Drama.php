        <section class="article-body has-media">
 <div class="article-pharagraph" id="node-0">
  <p class="text">
   Justin Sun has seized control of the Steem blockchain  with the apparent help of several prominent crypto exchanges.
  </p>
 </div>
 <div class="article-pharagraph" id="node-1">
  <p class="text">
   On the Steemit Blog (which is newly owned by Sun),
   <a data-reactroot="" href="https://steemit.com/tron/@steemitblog/an-open-letter-to-the-community-hf22-5" rel="noopener noreferrer" target="_blank">
    a post
   </a>
   announced the new regime:
  </p>
 </div>
 <div class="article-pharagraph" id="node-2">
  <p class="text">
   "For the next 4-6 weeks, the Steemit team will be using the voting rights to resume the order of the community while having an open channel for meeting community members and Witnesses."
  </p>
 </div>
 <div class="article-pharagraph" id="node-3">
  <p class="text">
   However, as of this writing, users are telling us of app instability and users on Steemit are signaling outright revolt. This is
   <a data-reactroot="" href="http://archive.is/Qmdei" rel="noopener noreferrer" target="_blank">
    an archived version
   </a>
   of the original post because Steemit has been unstable.
  </p>
 </div>
 <div class="article-pharagraph" id="node-4">
  <p class="text">
   In a delegated proof-of-stake (DPoS) system such as Steem, this Orwellian update is only made possible by enough of the networks native currency, STEEM, being thrown behind a new set of blockchain validators.
  </p>
 </div>
 <div class="article-pharagraph" id="node-5">
  <p class="text">
   In short, exchanges have staked STEEM they controlled (seemingly from user accounts) to vote for new leadership.
  </p>
 </div>
 <div class="article-pharagraph" id="node-6">
  <p class="text">
   The implications reach much further than just Steem or Tron, underlining the fundamental message of the "
   <a data-reactroot="" href="/upbit-is-the-sixth-major-crypto-exchange-hack-of-2019">
    not your keys, not your crypto
   </a>
   " mantra. When users hold large amounts of their assets on exchanges, it gives those firms potentially decisive power over ostensibly decentralized networks, particularly when governance authority is tied to currency holdings.
  </p>
 </div>
 <div class="article-heading" id="node-7">
  <h4>
   <strong>
    Wait, what?
   </strong>
  </h4>
 </div>
 <div class="article-pharagraph" id="node-8">
  <p class="text">
   The turmoil began on Feb. 14 when it was announced Sun, the controversial founder of the Tron blockchain, had acquired Steemit, a blogging site that owns a very large quantity of the STEEM cryptocurrency.
  </p>
 </div>
 <div class="article-pharagraph" id="node-9">
  <p class="text">
   In response, STEEM community leaders, the validators of the blockchain,
   <a data-reactroot="" href="/justin-sun-bought-steemit-steem-moved-to-limit-his-power">
    initiated a soft fork on Feb. 23
   </a>
   that censored the stake of tokens held by Steemit, usually referred to in the community as the "ninja-mined stake," as CoinDesk previously reported.
  </p>
 </div>
 <div class="article-pharagraph" id="node-10">
  <p class="text">
   "It's like the rich guys getting together and saying 'let's show them who's the real boss,'" Roeland Lanparty, a pseudonymous Dutch citizen who runs a Steem witness and some major applications, told CoinDesk over a WhatsApp call.
  </p>
 </div>
 <div class="article-pharagraph" id="node-11">
  <p class="text">
   Leaders or validators on Steem are called witnesses, serving similar roles to bitcoin's miners. All 20 of the new witnesses now leading the chain are accounts created in February 2020.
  </p>
 </div>
 <div class="article-pharagraph" id="node-12">
  <p class="text">
   Tron, Steemit and Huobi have not replied to multiple requests for comment. While Binance has not responded directly to CoinDesk's queries,
   <a data-reactroot="" href="https://twitter.com/cz_binance/status/1234565384327585792" rel="noopener noreferrer" target="_blank">
    an enigmatic tweet
   </a>
   from its CEO, Changpeng Zhao, addresses the issue.
  </p>
 </div>
 <div class="article-pharagraph" id="node-13">
  <p class="text">
   "I was made aware of this upgrade/hardfork beforehand, and approved it. Projects do this all the time, and we are usually just in a supportive position," he wrote. "Just messaged @justinsuntron, no response yet. Assume he will respond publicly soon."
  </p>
 </div>
 <div class="article-pharagraph" id="node-14">
  <p class="text">
   There has been no update on the
   <a data-reactroot="" href="https://steemit.com/steemit/@steemitblog/update-townhall-and-witness-meeting" rel="noopener noreferrer" target="_blank">
    proposed town hall
   </a>
   scheduled for March 6. STEEM value has been generally down since the acquisition, trading at roughly $0.23 before the news and now at around $0.17, according to
   <a data-reactroot="" href="https://coinmarketcap.com/currencies/steem/" rel="noopener noreferrer" target="_blank">
    CoinMarketCap
   </a>
   .
  </p>
 </div>
 <div class="article-heading" id="node-15">
  <h4>
   <strong>
    Steemit strikes back
   </strong>
  </h4>
 </div>
 <div class="article-pharagraph" id="node-16">
  <p class="text">
   The letter posted to the Steemit blog describes how Steemit had previously used its stake to support the community's development and says Suns Tron Foundation "intends to use part of the Steemit stake for such executions."
  </p>
 </div>
 <div class="article-pharagraph" id="node-17">
  <p class="text">
   It then writes that "the Witnesses decision [to soft fork] created a need to reclaim the stake and vote in new witnesses to usher in new policies for a healthier ecosystem and community," also adding that the move "may be deemed illegal and criminal."
  </p>
 </div>
 <div class="article-pharagraph" id="node-18">
  <p class="text">
   The post details several priorities for Steem going forward. It lists swaps between Steem and Tron,
   <a data-reactroot="" href="https://www.prnewswire.com/news-releases/steemits-smart-media-tokens-live-on-testnet-300938903.html" rel="noopener noreferrer" target="_blank">
    smart media tokens
   </a>
   (a project the company has discussed for
   <a data-reactroot="" href="https://observer.com/2017/10/steem-basic-attention-token/" rel="noopener noreferrer" target="_blank">
    at least two years
   </a>
   ), feature parity with Reddit and incentives for users to bring more people onto the platform.
  </p>
 </div>
 <div class="article-pharagraph" id="node-19">
  <p class="text">
   The new witnesses all now have
   <a data-reactroot="" href="https://steemian.info/witnesses" rel="noopener noreferrer" target="_blank">
    overwhelming support
   </a>
   in terms of STEEM-denominated votes; however, the number of user accounts backing them is tiny, as can be seen in the "voters total" column on that same page.
  </p>
 </div>

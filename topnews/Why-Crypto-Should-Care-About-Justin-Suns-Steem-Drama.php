 <div class="article-pharagraph" id="node-21">
  <p class="text">
   Overnight, Binance, Huobi and Poloniex (which is
   <a data-reactroot="" href="/despite-denials-tron-founder-confirms-investment-in-poloniex-crypto-exchange">
    partially owned by Sun
   </a>
   ) staked tokens they controlled in order to vote for a new slate of witnesses. Presumably, the lion's share of these tokens are technically owned by users of these platforms, not the exchanges themselves.
  </p>
 </div>
 <div class="article-pharagraph" id="node-22">
  <p class="text">
   A tweet
   <a data-reactroot="" href="https://twitter.com/jeffjagoe/status/1234483951185645574" rel="noopener noreferrer" target="_blank">
    shows large accounts
   </a>
   powering up tokens in advance of voting in the new slate of witnesses. Luke Stokes, a witness who has tracked exchange data for STEEM for years, verified that the accounts were related to these exchanges.
  </p>
 </div>
 <div class="article-pharagraph" id="node-23">
  <p class="text">
   Then the accounts enacted a new version of Steem software, version 22.5, which released the tokens controlled by Steemit. In large part these tokens quickly flowed into exchange accounts allowing them to increase their vote for Steem's new leaders.
  </p>
 </div>
 <div class="article-pharagraph" id="node-24">
  <p class="text">
   For context, the Steemit STEEM holdings have been an issue within the community for some time. Lanparty said Witnesses have previously discussed hard forks in order to remove the threat of a takeover by the company.
  </p>
 </div>
 <div class="article-pharagraph" id="node-25">
  <p class="text">
   Stokes broke down the mechanics of the takeover last night on YouTube:
  </p>
 </div>
 <div class="youtube-video" id="node-26">
  <iframe allow="accelerometer; encrypted-media; gyroscope; picture-in-picture" allowfullscreen="" src="https://www.youtube-nocookie.com/embed/8oh6Uw_ZH2A?start=0&amp;autoplay=0">
  </iframe>
 </div>
 <div class="article-pharagraph" id="node-27">
  <p class="text">
   Steem was created in part by Dan Larimer who also co-created EOS, which has a very similar structure. Notably, EOS's creator, Block.One, also holds a
   <a data-reactroot="" href="/block-one-is-taking-a-bigger-role-with-eos-and-thats-a-big-deal">
    potentially controlling portion
   </a>
   of EOS tokens, but this has not been a major point of contention within the EOS community.
  </p>
 </div>
 <div class="article-heading" id="node-28">
  <h4>
   <strong>
    What are users doing?
   </strong>
  </h4>
 </div>
 <div class="article-pharagraph" id="node-29">
  <p class="text">
   Lanparty told CoinDesk he was nervous about the Tron acquisition from the beginning. What alarmed him in particular, he said, was any discussion of token swaps in initial announcements of the Steemit acquisition.
  </p>
 </div>
 <div class="article-pharagraph" id="node-30">
  <p class="text">
   As well as running a witness, Lanparty organizes the annual SteemFest, the gathering of the blockchain's enthusiasts.
  </p>
 </div>
 <div class="article-pharagraph" id="node-31">
  <p class="text">
   "This is really a blockchain of people. This is a social chain," he said. "You get a little bit tangled up in all these people. It's not a gambling bot."
  </p>
 </div>

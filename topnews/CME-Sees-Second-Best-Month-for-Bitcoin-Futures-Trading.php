      hugely underwhelmed since its launch in September
     </a>
     , particularly in comparison with CME. The strong start to CME futures options has helped
     <a data-mil="39786" href="https://cryptocurrencynews.com/bitcoin-2-month-high-cme-options/" style="color: #000000;">
      drive a resurgence in Bitcoin (BTC) value
     </a>
     . After plummeting to $6,860 earlier in the year, the worlds leading cryptocurrency bounced back and is currently staring at a 3-month peak of over $10,200.
    </span>
   </p>
   <p>
    <span style="color: #000000;">

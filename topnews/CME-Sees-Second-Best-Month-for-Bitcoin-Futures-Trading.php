   <p>
    <span style="color: #000000;">
     January was the second-best month for CMEs Bitcoin futures trading since its 2017 launch, averaging 10,800 contracts, 69% greater than the average daily volume in 2019. Last month also saw new
     <a href="https://cryptonews.com/news/cme-doubles-bitcoin-options-volume-leaving-bakkt-in-the-dust-5568.htm" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      options on Bitcoin futures
     </a>
     picking up pace. Since launch, 466 options contracts, or 2,300 equivalent Bitcoin, have tradednearly 40% as blocks, showing signs of institutional interest, CME said. It also registered a record monthly average of 56 large open interest holders (entities that hold 25+ contracts).
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     The Chicago Mercantile Exchange (CME) launched its Bitcoin futures options last month, with
     <a href="https://www.theblockcrypto.com/post/52891/cme-tops-bakkts-reported-bitcoin-options-volumes-in-one-day" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      US$2.3 million
     </a>
     <a href="https://www.theblockcrypto.com/post/52891/cme-tops-bakkts-reported-bitcoin-options-volumes-in-one-day" style="color: #000000;">
      traded on the first day alone
     </a>
     , eclipsing the slow start made by rival exchange Bakkt, which is headed up by the parent company of the New York Stock Exchange, the Intercontinental Exchange (ICE). Both platforms were launched with the intention of increasing institutional investment in cryptocurrencies, essentially trying to make the trading of digital assets more mainstream.
    </span>
   </p>

   <p>
    <span style="color: #000000;">
     CME also revealed that it has seen
     <a data-mil="39786" href="https://cryptocurrencynews.com/bitcoin-futures-cme-trading-100-billion-2017/" style="color: #000000;">
      a total trading volume of over US$100 billion since its launch
     </a>
     in December 2017, which is an overwhelming endorsement of institutional interest in crypto trading. Tim McCourt, Group Managing Director of CME, also said that the exchange operates the most liquid Bitcoin derivatives markets in the world, which is an indication of market maturity, something that institutions generally look out for and could be a factor in driving higher trading volumes in future.
    </span>

   <p>
    <span style="color: #000000;">
     <a data-mil="39882" href="https://cryptocurrencynews.com/vodafone-leaves-libra-association/" style="color: #000000;">
      Vodafone was the most recent firm to quit The Libra Association
     </a>
     , although for a different reason. The British telecom giant said it intends to dedicate resources previously allocated for the project to its own well-established digital payment service M-Pesa, which it plans to expand beyond the six African nations currently served.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     In a rare piece of good news for Libra, Shopify said it is linking up with the venture to
     <em>
      work collectively to build a payment network that makes money easier to access and supports merchants and consumers everywhere.
     </em>
     The e-commerce platform has around
     <a href="https://www.forbes.com/sites/darrynpollock/2020/02/21/shopify-joins-libra-association-after-raft-of-departures/" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      1 million businesses from 175 countries on its books
     </a>
     and will join other Libra Association members in contributing at least US$10 million and operating a node that processes transactions for Libra, and will also leverage its expertise in managing payment networks.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     &gt;&gt;
     <a data-mil="39882" href="https://cryptocurrencynews.com/whale-transfer-ripple-xrp-bitstamp/" style="color: #000000;">
      Whale Transfers 31.30 Million Ripple (XRP) to Bitstamp
     </a>
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Following the news that Shopify would join the Libra Association, Libras head of policy and communications Dante Disparte said the group was
     <em>
      proud
     </em>
     to welcome its newest member and talked up the troubled initiative.
     <em>
      Shopify joins an active group of Libra Association members committed to achieving a safe, transparent, and consumer-friendly implementation of a global payment system that breaks down financial barriers for billions of people,
     </em>
     Disparte said.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Libra is slated to launch in June of this year despite the huge amount of pushback against the project.

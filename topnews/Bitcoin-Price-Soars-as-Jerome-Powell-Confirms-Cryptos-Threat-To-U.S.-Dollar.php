  <h4>
   Chinas Belt and Road Initiative Expected To Utilize Crypto
  </h4>
  <p>
   An additional positive from this exchange is the stark reminder that crypto is about to become a mainstream technology for the worlds second-largest economy. China is planning to use blockchain
   <a data-wpel-link="external" href="https://www.cfr.org/backgrounder/chinas-massive-belt-and-road-initiative" rel="nofollow external noopener noreferrer" target="_self">
    in its belt and road initiative
   </a>
   .
  </p>
  <p>
   <amp-youtube data-videoid="EvXROXiIpvQ" height="563" layout="responsive" width="1000">

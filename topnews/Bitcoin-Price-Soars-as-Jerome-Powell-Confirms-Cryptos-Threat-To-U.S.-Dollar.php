  <p>
   Powell has been quite open about bitcoin in the past,
   <a data-wpel-link="internal" href="https://www.ccn.com/fed-chair-suggests-bitcoin-is-golds-biggest-competition/" rel="noopener" target="_self">
    even acknowledging that it is a store of value like gold
   </a>
   . Today he further demonstrated the high level of intellectual investment that the Federal Reserve is making into cryptocurrency.
  </p>

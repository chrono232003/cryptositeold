  <p>
   An otherwise dull testimony from Federal Reserve Chairman Jerome Powell got very interesting for bitcoin, as BTC/USD spiked 4% to $10,200.
  </p>
  <p>
   Cryptocurrency markets soared after Congressman Bill Foster raised concerns about the need to keep pace with Chinas digital currency ambitions to which Powell responded that the Fed has a lot of projects underway.

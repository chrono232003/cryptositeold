  <p>
   So perhaps John McAfee has a point when he says the future of cryptocurrency rests with altcoins. Although, that does nothing to narrow down the over 5,000 altcoins that exist at the current time, as per CoinMarketCap.
  </p>
  <p>
   McAfees claim that BTC lacks security rings false. Bitcoin is better secured by hash power than any other coin the space. Other altcoins even
   <a data-wpel-link="external" href="https://hacked.com/blockchain-in-sixty-seconds-komodo-launches-diy-smart-chain-builder/" rel="nofollow external noopener noreferrer" target="_self">
    back up their own blockchain on BTCs
   </a>
   for added security.
  </p>
  <p>
   But on just about every other front, the characterization of bitcoin as the hotmail of the cryptocurrency world rings true.
  </p>

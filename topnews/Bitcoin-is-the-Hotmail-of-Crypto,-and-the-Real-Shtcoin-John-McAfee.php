    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="1136877805" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   As McAfee points out, BTC is indeed old and clunky. Its on-chain transaction rate hasnt expanded above 7 transactions per second in years. Whenever a large enough user base
   <i>
    does
   </i>
   decide to utilize bitcoin, it inevitably
   <a data-wpel-link="external" href="https://themerkle.com/bitcoin-price-struggles-for-momentum-as-network-remains-clogged-up-and-fees-high/" rel="nofollow external noopener noreferrer" target="_self">
    chokes and stutters under the weight of demand
   </a>
   .
  </p>
  <h2>
   Hotmail of Cryptocurrency World
  </h2>
  <p>
   McAfee
   <a data-wpel-link="external" href="https://twitter.com/officialmcafee/status/1222395079454838785?s=20" rel="nofollow external noopener noreferrer" target="_self">
    quoted a Twitter followers descriptions of bitcoin
   </a>
   , stating: Bitcoin is the Hotmail of crypto.
  </p>
  <figure aria-describedby="caption-attachment-230873" class="wp-caption alignnone acss9d4ce" id="attachment_230873">
   <amp-img alt="John MCAfee, Bitcoin, BTC, Altcoins" class="size-full wp-image-230873 amp-wp-enforced-sizes" height="300" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1.png" srcset="https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1.png 886w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1-300x102.png 300w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1-768x260.png 768w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1-800x271.png 800w" width="886">
    <amp-img alt="John MCAfee, Bitcoin, BTC, Altcoins" class="size-full wp-image-230873 amp-wp-enforced-sizes" fallback="" height="300" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg 886w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1-300x102.png 300w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1-768x260.png 768w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee2-1-800x271.png 800w" width="886">

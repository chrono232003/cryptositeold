   <figcaption class="wp-caption-text" id="caption-attachment-230873">
    McAfee compared bitcoin unfavorably to hotmail  the email provider of yesteryear. | Source: @OfficialMcAfee,
    <a data-wpel-link="external" href="https://twitter.com/officialmcafee/status/1222389224902668288?s=20" rel="nofollow external noopener noreferrer" target="_self">
     Twitter
    </a>
   </figcaption>
  </figure>
  <p>
   Its also true that BTC doesnt host smart contracts or decentralized applications (Dapps) at the current time. In all fairness, theres no clear guarantee that the future success of any given coin will be decided by smart contracts or Dapps.
  </p>
  <p>
   It would be nice to be able to say that BTC was focused on becoming real, hard currency, but thats not really the case either. The developers insist
   <a data-wpel-link="external" href="https://news.bitcoin.com/putting-an-end-to-the-bitcoin-store-of-value-fallacy/" rel="nofollow external noopener noreferrer" target="_self">
    BTC is to become a store of value for the elite
   </a>
   , rather than a transactional currency for everyday users.
  </p>

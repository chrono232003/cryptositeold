  <p>
   Bitcoin (BTC) is the hotmail of the cryptocurrency world. Thats according to altcoin king, and self-declared U.S. presidential candidate, John McAfee.
  </p>
  <h2>
   Bitcoin Is Sh*tcoin, Not Altcoins
  </h2>
  <p>
   McAfee took to Twitter on Wednesday to relieve his followers of the propaganda instilled in them by BTC maximalists.
   <a data-wpel-link="external" href="https://twitter.com/officialmcafee/status/1222389224902668288?s=20" rel="nofollow external noopener noreferrer" target="_self">
    McAfee pointed out the genius of characterizing the entire altcoin market as sh*tcoins
   </a>
   , but said that term only really applies to bitcoin.
  </p>
  <figure aria-describedby="caption-attachment-230872" class="wp-caption alignnone acss9d4ce" id="attachment_230872">
   <amp-img alt="Bitcoin, John McAfee, BTC, Altcoins" class="wp-image-230872 size-full amp-wp-enforced-sizes" height="753" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1.png" srcset="https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1.png 886w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1-300x255.png 300w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1-768x653.png 768w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1-800x680.png 800w" width="886">
    <amp-img alt="Bitcoin, John McAfee, BTC, Altcoins" class="wp-image-230872 size-full amp-wp-enforced-sizes" fallback="" height="753" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg 886w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1-300x255.png 300w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1-768x653.png 768w, https://www.ccn.com/wp-content/uploads/2020/01/mcafee1-1-800x680.png 800w" width="886">
    </amp-img>
   </amp-img>
   <figcaption class="wp-caption-text" id="caption-attachment-230872">
    John McAfee says its bitcoin thats the real sh*tcoin, not altcoins which carry a number of additional features. | Source: OfficialMcAfee,
    <a data-wpel-link="external" href="https://twitter.com/officialmcafee/status/1222389224902668288?s=20" rel="nofollow external noopener noreferrer" target="_self">
     Twitter
    </a>
   </figcaption>
  </figure>
  <p>
   A sh*tcoin, according to BTC maximalists, is any cryptocurrency that isnt bitcoin.
   <a data-wpel-link="exclude" href="https://www.reddit.com/r/Buttcoin/" rel="follow noopener" target="_self">
    Buttcoinis what people who hate all cryptocurrencies
   </a>
   call bitcoin, but thats a separate matter.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">

  <h2>
   Bitcoin Cash Hardforkers Stand Down
  </h2>
  <p>
   The
   <a data-wpel-link="external" href="https://read.cash/@shadow-kwh/bch-dev-fund-a-response-from-an-opposing-mining-group-see-update-inside-4397e64b" rel="nofollow external noopener noreferrer" target="_self">
    anonymous mining group which threatened the chain split
   </a>
   took notice of Bitcoin.coms reversal. The group
   <a data-wpel-link="exclude" href="https://www.reddit.com/r/btc/comments/ev1ep1/we_are_a_group_of_miners_opposing_the_btctop/" rel="follow noopener" target="_self">
    announced their intention to halt plans for a hardfork
   </a>
   , and will continue to support BCH for the time being.
  </p>
  <blockquote>
   <p>
    We have taken notice of Bitcoin.com post
    <a data-wpel-link="external" href="https://read.cash/@Bitcoin.com/update-on-developer-funding-83695ebc" rel="nofollow external noopener noreferrer" target="_self">
     here
    </a>
    . We trust Bitcoin.com are going to be able to convince the rest of the signatories to severely amend the IFP. We are therefore standing down and will not start our competing pool for the time being and will continue to support the BCH pools instead.
   </p>
  </blockquote>
  <p>
   The tax proposal triggered vigorous debate among developers, miners and investors in the week since its announcement. Originally declared set in stone by its author, Jiang Zhuoer, the plan attracted criticism on a number of fronts.
  </p>
  <p>
   But the turning point for Bitcoin.com came when the prospect of another chain split reared its head. Both Bitcoin Cash (BCH) and Bitcoin SV (BSV) spent tens of millions competing for hashrate in 2018.
  </p>
  <p>
   The previous war also had a disastrous effect on cryptocurrency prices, not least Bitcoin Cash itself. The coin lost 87% of its value in five weeks in November of that year. A repeat of this messy affair is sought by no one in the cryptocurrency space.
  </p>

  <p>
   Bitcoin.com  one of the five mining pools backing the plan  has withdrawn its support for the tax plan. The sudden u-turn comes just hours after an anonymous group of
   <a data-wpel-link="internal" href="https://www.ccn.com/hash-war-looms-as-bitcoin-cash-tax-drama-explodes/" rel="noopener" target="_self">
    miners threatened to launch a hash war
   </a>
   in opposition to the tax.
  </p>
  <h2>
   Hash War Averted For Now
  </h2>
  <p>
   Tuesdays
   <a data-wpel-link="external" href="https://read.cash/@Bitcoin.com/update-on-developer-funding-83695ebc" rel="nofollow external noopener noreferrer" target="_self">
    post by BCH mining pool, Bitcoin.com
   </a>
   , announced the firms re-thinking of the BCH tax proposal. While the possibility of another chain split remains possible, Bitcoin.com will not continue to support the tax plan.
  </p>

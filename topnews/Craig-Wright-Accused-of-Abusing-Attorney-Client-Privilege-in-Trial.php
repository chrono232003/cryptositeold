   <p>
    <span style="color: #000000;">
     The case against Craig Wright has been
     <a data-mil="39691" href="https://cryptocurrencynews.com/binance-delists-bitcoin-sv/" style="color: #000000;">
      littered with controversy
     </a>
     throughout the entirety of proceedings. Last August, Judge Bruce E. Reinhart ruled in favor of the Kleiman estate, admonishing Wright at the time for engaging in a
     <em>
      willful and bad faith pattern of obstructive behavior,
     </em>
     which included filing a false declaration, knowingly producing a fraudulent trust document, and giving perjurious testimony.
    </span>
   </p>
   <p>

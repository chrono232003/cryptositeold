   <p>
    <span style="color: #000000;">
     Craig Wright, the self-professed creator of Bitcoin (BTC), has been
     <a href="https://cointelegraph.com/news/craig-wright-accused-of-confusing-trial-proceedings" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      accused of abusing attorney-client privilege
     </a>
     in order to block documents from being used as evidence in the ongoing trial against him.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Australian computer scientist and businessman Craig Wright is being sued by the family of his deceased business partner Dave Kleiman for US$10 billion in Bitcoin, said to be encrypted in a
     <em>
      Tulip Trust.
     </em>
     In a court document filed on Sunday in Southern District Court of Florida, the legal team representing Ira Kleiman, brother of the deceased, challenged Wrights illusory assertion that over 11,000 company documents are inadmissible in court due to coming under attorney-client privilege.
    </span>

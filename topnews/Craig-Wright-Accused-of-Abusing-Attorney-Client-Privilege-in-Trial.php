   <p>
    <span style="color: #000000;">
     <em>
      Craig [Wright] has improperly withheld documents as privileged,
     </em>
     stated the prosecution, arguing that he has attempted to obfuscate the trial by using the fact that the documents come from 18 companies, most of them defunct, to hide the documents from the court.
     <em>
      [A]lmost all of the companies Defendant is using as a tool to assert privileges, no longer exist  any privilege these companies may have once had, does not survive their dissolution,
     </em>
     added Kleimans legal team.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     The filing continues to say that of the three remaining firms, Wrights privilege claim is inadmissible due to the fact that he is not in the control group and therefore, cannot claim attorney-client privilege. He is also accused of purposefully orchestrating not being on leadership teams at the firms so as to be able to claim privilege
     <em>
      when it suits him.
     </em>
    </span>
   </p>

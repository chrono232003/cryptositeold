   spreads.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-1">
  <p class="text">
   The largest cryptocurrency by market value rose 4.1 percent to $8,874 as of 17:53 UTC (12:53 p.m. ET). The move followed last weeks 14 percent sell-off, the harshest in three months.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-2">
  <p class="text">
   Despite a belief among some investors that bitcoin should trade as a
   <a data-reactroot="" href="/bitcoin-as-a-safe-haven-us-iran-tensions-rekindle-debate">
    safe-haven asset
   </a>
   akin to gold or U.S. Treasury bonds, the cryptocurrency had tumbled recently along with riskier assets like stocks. The sell-off reversed as global authorities, including the Fed, Bank of Japan, International Monetary Fund and World Bank, pledged to act if needed to help offset any lasting economic damage from widespread travel cancellations, quarantines and factory disruptions.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-3">
  <p class="text">
   Investors are dipping their toes back in the water after last week [when] they jumped out of the pool, said John Todaro, director of currency research at the crypto-focused firm TradeBlock in New York. This easy monetary policy across central banks should continue to support markets in general, especially in risk-on areas like equity markets, and that should bleed into other asset classes like digital currencies, including bitcoin.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-4">
  <p class="text">
   <a data-reactroot="" href="https://www.federalreserve.gov/newsevents/pressreleases/other20200228a.htm" rel="noopener noreferrer" target="_blank">
    Fed Chair Jerome Powell said
   </a>
   Friday in a statement the central bank would use our tools and act as appropriate to support the economy.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-5">
  <p class="text">
   Trading in the Chicago Mercantile Exchanges market for futures contracts on the Feds benchmark interest rate shows that investors
   <a data-reactroot="" href="https://www.cmegroup.com/trading/interest-rates/countdown-to-fomc.html" rel="noopener noreferrer" target="_blank">
    now see a 100 percent chance
   </a>
   of a 0.5 percentage point cut at the central banks next regular monetary-policy decision, scheduled for March 18. The rate is currently set in a range between 1.5 percent and 1.75 percent.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-6">
  <p class="text">
   Bank of Japan Governor Haruhiko Kuroda said Monday the central bank would strive to stabilize markets and offer
   <a data-reactroot="" href="https://www.reuters.com/article/us-health-coronavirus-japan/boj-reassurance-on-coronavirus-bolsters-speculation-of-global-policy-action-idUSKBN20P018" rel="noopener noreferrer" target="_blank">
    sufficient liquidity
   </a>
   via market operations and asset purchases, Reuters reported.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-7">
  <p class="text">
   The heads of the International Monetary Fund and World Bank said Monday their institutions stood ready to provide
   <a data-reactroot="" href="https://www.imf.org/en/News/Articles/2020/03/02/pr2076-joint-statement-from-imf-managing-director-and-wb-president" rel="noopener noreferrer" target="_blank">
    emergency financing
   </a>
   , policy advice and technical assistance, with special attention to poor countries where health systems are the weakest and people are most vulnerable.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-8">
  <p class="text">
   The global number of infections is approaching 90,000, according to the
   <a data-reactroot="" href="https://www.nytimes.com/2020/03/02/world/coronavirus-news.html" rel="noopener noreferrer" target="_blank">
    New York Times
   </a>
   . The first two deaths from the outbreak have now occurred in the U.S., with new cases officially reported in New York, Rhode Island and Florida.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-9">
  <p class="text">
   Economists with the Organization of Economic Cooperation and Development said Monday in an assessment that the contagion likely will shave 0.5 percentage point off of projected 2020 global growth, from an
   <a data-reactroot="" href="https://www.oecd-ilibrary.org/docserver/7969896b-en.pdf?expires=1583174660&amp;id=id&amp;accname=guest&amp;checksum=10591FF9AB772474F51BE398D2795B99" rel="noopener noreferrer" target="_blank">
    already weak
   </a>
    2.9 percent.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-10">
  <p class="text">
   The adverse impact on confidence, financial markets, the travel sector and disruption to supply chains contributes to the downward revisions, the organization said.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-11">
  <p class="text">
   The rebound in bitcoin left prices up 24 percent so far this year  markedly better than the performance in the S&amp;P 500, which was still down 6.8 percent in 2020.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-12">
  <p class="text">
   Some investors say bitcoin should serve as a
   <a data-reactroot="" href="https://www.forbes.com/sites/matthougan/2019/08/21/bitcoin-vs-gold-is-bitcoin-really-a-new-safe-haven-asset/#2c8a0c6c7ba1" rel="noopener noreferrer" target="_blank">
    hedge against economic turmoil
   </a>
   , partly because its perceived as immune to the cycles that have emerged in traditional markets, where central banks are expected to come to the rescue in the face of a sell-off. The idea is that
   <a data-reactroot="" href="https://medium.com/@BitBullCapital/the-case-for-bitcoin-as-a-hedge-against-hyperinflation-66fabd86eef8" rel="noopener noreferrer" target="_blank">
    money-printing will stoke inflation
   </a>
   , reducing the purchasing power of government-issued currencies like the U.S. dollar.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-13">
  <p class="text">
   The issuance of new bitcoins is dictated by the 11-year-old blockchain networks original programming, and cryptocurrency speculators have pegged their hopes to the upcoming halving expected in May  a once-every-four-years, 50 percent reduction in the pace of new supplies.
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-14">
  <p class="text">
   Analysts for the German bank BayernLB predicted last year that the halving could send
   <a data-reactroot="" href="https://www.bayernlb.com/internet/media/ir/downloads_1/bayernlb_research/megatrend_publikationen/megatrend_bitcoins2f_20190930_EN.pdf" rel="noopener noreferrer" target="_blank">
    bitcoins price to $90,000
   </a>
   .
   <br/>
  </p>
 </div>
 <div class="article-pharagraph" id="node-15">
  <p class="text">
   But traders in the nascent
   <a data-reactroot="" href="/bitcoins-option-market-sees-low-chance-of-post-halving-rally">
    market for bitcoin options
   </a>
   are split on whether prices will rise above current levels over the next six months. According to Skew, a cryptocurrency-derivatives research firm, pricing in the options market suggests its a coin toss whether bitcoin will trade above the $8,000 mark at the end of September.
   <br/>
  </p>

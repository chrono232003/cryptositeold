  <p>
   With the pullback, bitcoin has erased more than a third of its 2020 rally. The cryptocurrency topped $10,500 during the high point of its rally earlier this month. At the time, several analysts talked up the possibility of breaching $11,000, but a series of lower highs paved the way for Wednesdays eventual retreat.
  </p>

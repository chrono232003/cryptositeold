    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   To be fair, bitcoin and gold have shown stronger correlation in recent months, especially as tensions in the Middle East escalated and the health of global economies looked to be in jeopardy. Still, bitcoins failure to attract bids during
   <a data-wpel-link="exclude" href="https://www.bloomberg.com/quote/VIX:IND" rel="follow noopener" target="_self">
    one of the most volatile periods in years
   </a>
   pours cold water on claims of it being a reliable safe-haven asset.
  </p>
  <p>
   The stock-market selloff showed signs of abating Wednesday, though the Dow found itself struggling to stay positive. At its lowest point, the index was down by 111 points.
  </p>
  <p>
   Meanwhile, the price of gold held steady near $1,649 an ounce in New York trading. The yellow metal peaked north of $1,690 earlier in the week.

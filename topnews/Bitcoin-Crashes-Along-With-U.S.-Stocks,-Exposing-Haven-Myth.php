   At current values, bitcoin has a market capitalization of $160.1 billion, the lowest in a month.
  </p>
  <p>
   The market for altcoins and tokens also declined sharply. At last check, the
   <a data-wpel-link="external" href="https://coinmarketcap.com/" rel="nofollow external noopener noreferrer" target="_self">
    total value of all cryptocurrencies
   </a>
   was down nearly $20 billion to $250.6 billion.
  </p>
  <h2>
   Bitcoin Lacks Haven Appeal as Investors Flee Risk Assets
  </h2>
  <p>
   <amp-youtube data-videoid="ujMp7ZqDSX4" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   Bitcoin is selling off in lockstep with U.S. equities, as investors reduce their exposure to risk over the global coronavirus outbreak. The Dow plunged another 878 points on Tuesday, capping off its worst two-day performance in years. Just 24 hours prior, the blue-chip index closed down more than 1,000 points.
  </p>
  <p>
   Precious metals have benefited from the coronavirus-fueled selloff, with
   <a data-wpel-link="internal" href="https://www.ccn.com/gold-price-could-skyrocket-as-a-stock-market-crash-looks-imminent/" rel="noopener" target="_self">
    gold setting fresh seven-year highs
   </a>
   . If bitcoin were digital gold as
   <a data-wpel-link="internal" href="https://www.ccn.com/10000-bitcoin-looks-imminent-based-on-this-one-crucial-chart/" rel="noopener" target="_self">
    many of its proponents claim
   </a>
   , this weeks events should have triggered a buying spree of the virtual asset. Instead, the complete opposite has happened.
  </p>

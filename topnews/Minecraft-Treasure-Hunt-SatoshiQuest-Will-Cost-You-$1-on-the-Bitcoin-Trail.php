  <p>
   With that in mind, it may be wise to take advantage of this Bitcoin Minecraft server sooner rather than later.
  </p>
  <p>
   As early as 2015 Bitcoin was already being incorporated into Minecraft servers. However, the coins meteoric rise over the next few years came with rising transaction fees that made it untenable for such use-cases.
  </p>
  <p>
   <a data-wpel-link="external" href="https://xbt.net/blog/bitcoin-blog/minecraft-bitcoin-mining-is-done-what-happened-to-the-bitconomy/" rel="nofollow external noopener noreferrer" target="_self">
    PlayMC was once a popular Minecraft server
   </a>
   [XBT.net] which awarded players satoshis (the smallest unit of Bitcoin) for completing in-game tasks. The server shut down in 2017  the year of Bitcoins greatest price increase to date.
  </p>
  <p>
   Minecraft servers incorporating various altcoins also litter the history of cryptocurrency. Both Dogecoin (DOGE) and DigiByte (DGB) servers have come and gone in previous years. Few applications managed to gain popularity or withstand the constantly shifting cryptocurrency landscape.
  </p>

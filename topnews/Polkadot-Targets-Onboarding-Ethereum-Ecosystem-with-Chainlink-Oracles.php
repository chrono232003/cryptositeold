   <p>
    <span style="color: #000000;">
     Polkadot is readying itself for its expected network launch after it announced its integration with Chainlink oracles on February 25. The progress made with the integrations is an indication of a systematic approach that will allow porting with Ethereum infrastructure.
    </span>
   </p>
   <h4>
    <span style="color: #000000;">
     Polkadot Announces Integrations of Chainlink Oracles
    </span>
   </h4>
   <p>
    <span style="color: #000000;">
     The integration of Chainlink oracles is vital in the creation of Polkadots decentralized finance and other smart contracts. Chainlink has finalized the preliminary integration on Kusama, which is a Polkadot canary network synonymous with a testnet.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Following the move, Polkadot developers will now be able to get
     <a href="https://cointelegraph.com/news/polkadot-aims-for-ethereum-ecosystem-with-chainlink-integration" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      access to external data through Chainlink oracles
     </a>
     . This is important, and it will enable most of the more advanced features connected with smart contracts. The nature of the operation of oracles is through the transmission of real-world information to a blockchain in a form that a smart contact will understand and thus act on that data.
    </span>
   </p>

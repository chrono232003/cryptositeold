    </span>
   </p>
   <h4>
    <span style="color: #000000;">
     Parity Seeks to Onboard Ethereum Ecosystem Developers
    </span>
   </h4>
   <p>
    <span style="color: #000000;">
     Parity Technologies was the lead developer behind Polkadot because of its unique structure. Gavin Wood, the co-founder of Parity, was also among the co-founders of Ethereum. The Parity Ethereum client is one of the companys products used in connecting with peers and processing the blockchain.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     In December last year,
     <a href="https://www.parity.io/parity-ethereum-openethereum-dao/" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      Parity indicated that it will stop the development
     </a>
     of its client and spin-off to a decentralized autonomous organization. Parity and the Web3 foundation have been making moves to onboard Ethereum ecosystem developers to their network. The addition of Chainlink indicates that there is a way of porting over Ethereum infrastructure.
    </span>
   </p>

   <p>
    <span style="color: #000000;">
     Currently, the most prominent use of oracles is for price feeds of crypto and other digital assets. For instance, MakerDo depends on the ETH/USD price feed to create a DAU stablecoin despite being a different and exclusive oracle.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Chainlink will
     <a href="https://www.fortunecryptonews.com/2020/02/polkadot-aims-for-ethereum-ecosystem-with-chainlink-integration/" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      integrate on Polkadot with a committed parachain
     </a>
     , which refers to a blockchain chip that has personalized features. The other parachains on the interoperable network will access data from Chainlink chips to power dApps. The heterogeneous sharding approach will help solve most of the common blockchain problems, such as scalability, interoperability, and governance, as well as network security.
    </span>
   </p>

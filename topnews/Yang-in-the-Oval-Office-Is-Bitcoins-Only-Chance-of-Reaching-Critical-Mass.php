     Twitter
    </a>
   </figcaption>
  </figure>
  <p>
   Yang is potentially one of the biggest cryptocurrency and blockchain advocates a U.S. presidential campaign has ever witnessed. The Democratic candidate has actively and unabashedly
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-president-andrew-yang-fun-crypto-white-house/" rel="noopener" target="_self">
    spoken on the topic on more than one occasion
   </a>
   . Rather than shy away from the somewhat divisive subject, Yang has chosen to embrace it.
  </p>
  <p>
   In his
   <a data-wpel-link="exclude" href="https://www.yang2020.com/policies/digital-asset-regulation/" rel="follow noopener" target="_self">
    framework for digital asset policy and consumer protection
   </a>
   , Yang highlights the importance of uniformity, clarification, and regulatory reform:
  </p>
  <blockquote>
   <p>
    Investment in cryptocurrencies and digital assets has far outpaced our regulatory frameworks in the US. We should let investors, companies, and individuals know what the landscape and treatment will be moving forward to support innovation and development. The blockchain has vast potential.
   </p>
  </blockquote>
  <p>
   Yangs guidelines suggest creating a clear definition of which federal agencies have regulatory power over the crypto market, illuminating distinct rules under one national framework.
  </p>
  <p>
   If bitcoin and the broader crypto industry is to truly proliferate, fitting and unified regulation needs to be adopted. Arguably, Yang is the only one capable of bringing about that change.
  </p>

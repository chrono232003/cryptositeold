  <p>
   <span class="acss7a230">
    The U.S. regulatory landscape for cryptocurrencies is particularly uneven.
   </span>
   <span class="acss7a230">
    For a country priding itself as a leader in global market integrity, America is practically sitting on the sidelines when it comes to effective crypto legislation.
   </span>
  </p>
  <p>
   <span class="acss7a230">
    A vast number of agencies all vying for regulatory dominance has left the market kneecapped, hindering innovation and growth.
   </span>
  </p>
  <p>
   <span class="acss7a230">
    For example,
    <a data-wpel-link="internal" href="https://www.ccn.com/as-sec-debates-whether-cryptocurrencies-are-securities-initiative-seeks-to-distance-xrp-from-ripple/" rel="noopener" target="_self">
     while the SEC is concerned about the classification of specific cryptocurrencies
    </a>
    , the IRS distinguishes them all as
    <a data-wpel-link="exclude" href="https://www.irs.gov/pub/irs-drop/n-14-21.pdf" rel="follow noopener" target="_self">
     property for U.S. federal tax purposes
    </a>
    and
    <a data-wpel-link="exclude" href="https://www.cftc.gov/PressRoom/PressReleases/7820-18" rel="follow noopener" target="_self">
     the CFTC deems digital assets commodities
    </a>
    like gold and silver. This lack of a standard leaves companies blinded, unable to comply.
   </span>
  </p>
  <h2>
   Bitcoins Biggest Stigma
  </h2>
  <p>
   Whether fair or not, crypto criminality has long stood as a stigma overshadowing the nascent industry. In 2019,
   <a data-wpel-link="internal" href="https://www.ccn.com/two-groups-have-dominated-cryptocurrency-hacks-raking-in-1-billion-chainalysis/" rel="noopener" target="_self">
    multiple reports from various blockchain analytic companies
   </a>
   highlighted just how prevalent crime is within the crypto ecosystem. While researchers have observed that nefarious activity is but a drop in the bucket compared to the size of the market, manipulation, fraud and scams still stand as a significant barrier for entry for investors.
  </p>
  <p>
   In fact, a
   <a data-wpel-link="external" href="https://go.chainalysis.com/rs/503-FAP-074/images/2020-Crypto-Crime-Report.pdf" rel="nofollow external noopener noreferrer" target="_self">
    2020 report on the state of crypto crime
   </a>
   issued by crypto analytics firm, Chainaylisisfound that bitcoins use by darknet criminals had doubled from 2018 to 2019. Still, this only accounted for 0.08% of total bitcoin transactions last year.
  </p>

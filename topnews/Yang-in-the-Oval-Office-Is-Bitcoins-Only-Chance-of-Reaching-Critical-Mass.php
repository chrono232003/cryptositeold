  <p>
   According to the report, this is an issue that only adequate regulation can resolve:
  </p>
  <blockquote>
   <p>
    Cryptocurrency scams represent a significant danger to consumer protection, and the growth of this activity in 2019 calls for increased action from regulators, law enforcement and exchanges alike.
   </p>
  </blockquote>
  <h2>
   Is Yang Bitcoins Only Shot at Redemption?
  </h2>
  <figure aria-describedby="caption-attachment-231161" class="wp-caption alignnone acss53e54" id="attachment_231161">
   <amp-img alt="Andrew Yang on blockchain's &quot;wealth of potential&quot; | Source: Twitter" class="size-full wp-image-231161 amp-wp-enforced-sizes" height="198" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/Screen-Shot-2020-01-30-at-14.09.10.png" srcset="https://www.ccn.com/wp-content/uploads/2020/01/Screen-Shot-2020-01-30-at-14.09.10.png 599w, https://www.ccn.com/wp-content/uploads/2020/01/Screen-Shot-2020-01-30-at-14.09.10-300x99.png 300w" width="599">

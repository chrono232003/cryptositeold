   <br/>
   <p>
    <span style="color: #000000;">
     Vodafones decision to leave the Libra Association does not appear to be driven by regulatory concerns or scrutiny leveled at the project, unlike PayPal, Mastercard, or Visa, all of which cited the intense criticism of the proposed digital asset as a key factor in their decisions walk away. However, Vodafone is the first company to depart the association
     <a data-mil="39631" href="https://cryptocurrencynews.com/facebook-officially-launches-libra-geneva/" style="color: #000000;">
      since its official inception
     </a>
     in Switzerland last October.
    </span>
   </p>
   <h4>
    <span style="color: #000000;">
     Libra Down to 20For Now
    </span>
   </h4>
   <p>
    <span style="color: #000000;">
     In a statement, a Vodafone spokesperson said the company believes it can most effectively bring affordable financial services to the unbanked and underbanked by focusing on M-Pesa.
     <em>
      We have said from the outset that Vodafones desire is to make a genuine contribution to extending financial inclusion,
     </em>
     the spokesperson said.
     <em>
      We remain fully committed to that goal.
     </em>
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Now down to 20 members, The Libra Association said it intends to admit new members in 2020, with over 1,500 companies currently on a waitlist for membership. Dante Disparte, Libras communications chief, said,
     <em>
      although the makeup of the Association members may change over time, the design of Libras governance and technology ensures the Libra payment system will remain resilient.
     </em>
    </span>
   </p>
   <p>
    <span style="color: #000000;">

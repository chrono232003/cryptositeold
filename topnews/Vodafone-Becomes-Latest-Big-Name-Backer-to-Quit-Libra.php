    Vodafone Becomes Latest Big Name Backer to Quit Libra
   </h1>
  </header>
  <div class="entry-content clearfix custom-blogpost">
   <figure class="entry-thumbnail">
    <img alt="Libra" class="attachment-full size-full wp-post-image" height="436" sizes="(max-width: 777px) 100vw, 777px" src="https://cryptocurrencynews.com/wp-content/uploads/sites/3/2020/01/Libra-1-1.jpg" srcset="https://cryptocurrencynews.com/wp-content/uploads/sites/3/2020/01/Libra-1-1.jpg 777w, https://cryptocurrencynews.com/wp-content/uploads/sites/3/2020/01/Libra-1-1-300x168.jpg 300w, https://cryptocurrencynews.com/wp-content/uploads/sites/3/2020/01/Libra-1-1-768x431.jpg 768w, https://cryptocurrencynews.com/wp-content/uploads/sites/3/2020/01/Libra-1-1-678x381.jpg 678w" width="777">

      Chainlink (LINK) Soars 50% This Month: Here are the Key Drivers
     </a>
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     As M-Pesa currently accepts a number of different currencies for remittance, the possibility that it may accept cryptocurrencies, including Libra, remains open.
     <em>
      We will continue to monitor the development of the Libra Association and do not rule out the possibility of future cooperation,
     </em>
     a Vodafone spokesperson said.
    </span>
   </p>

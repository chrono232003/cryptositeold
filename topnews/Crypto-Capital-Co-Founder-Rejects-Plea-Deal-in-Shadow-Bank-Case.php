   <p>
    <span style="color: #000000;">
     Crypto Capital co-founder Reginald Fowler
     <a href="https://www.theblockcrypto.com/linked/54690/reggie-fowler-vikings-investor-crypto-shadow-bank-plea-reject" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      has rejected a plea deal
     </a>
     with the US government in an ongoing case in which he is accused of operating a
     <em>
      shadow bank
     </em>
     for several crypto exchanges.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Fowler was
     <a data-reactroot="" href="https://www.coindesk.com/two-charged-with-running-shadow-banking-service-for-crypto-exchanges" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      indicted
     </a>
     in April 2019, along with Israeli national Ravid Yosef, for allegedly running a shadow bank for several cryptocurrency firms, including Bitfinex and Tether, which are currently embroiled in a separate case in which they are accused of creating
     <em>
      the largest bubble in human history.
     </em>
     According to a letter submitted by the prosecution on January 31, the Crypto Capital co-founder
     <em>
      rejected the current plea offer,
     </em>
     and the US government
     <em>
      has formally withdrawn that offer.
     </em>
     The parties will now move to trial on April 28.
    </span>

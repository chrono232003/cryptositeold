  </p>
  <p>
   But the real controversy came hours later, when Sun attempted to absolve himself of any responsibility for removing the old hard cap. The TRON founder published a Medium blog where
   <a data-wpel-link="external" href="https://medium.com/tron-foundation/justin-sun-responds-to-trons-hard-cap-11293102a9bf" rel="nofollow external noopener noreferrer" target="_self">
    the reason for TRONs hard cap removal was explained
   </a>
   .
  </p>
  <h2>
   Justin Sun Lying About TRON Block Reward Reshuffle?
  </h2>
  <p>
   Sun pointed to last years reshuffling of the TRX block rewards, which increased the inflation rate to almost 2 billion TRX per year. Meanwhile, the TRON hard cap was removed completely.
  </p>
  <p>
   In the same post, Sun stated that the reorganization of TRONs block reward had nothing to do with him, or the TRON Foundation.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">

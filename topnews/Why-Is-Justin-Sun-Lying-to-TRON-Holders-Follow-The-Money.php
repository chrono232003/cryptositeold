    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   Rather, Sun states the decision to further inflate the TRX coin supply came from TRONs 27 Super Representatives. Sun wrote:
  </p>
  <blockquote>
   <p>
    The original, initial token issuance of 100 billion TRX is the only decision made by the TRON Foundation. The rest of the decisions including DPOS (delegated proof of stake) and the increase of the voting and block rewards have been made by the committee of Super Representatives.
   </p>
  </blockquote>
  <p>
   As early as last September,
   <a data-wpel-link="external" href="https://twitter.com/justinsuntron/status/1177417733777973249?s=20" rel="nofollow external noopener noreferrer" target="_self">
    Sun was already promoting the change in block rewards on social media
   </a>
   .
  </p>

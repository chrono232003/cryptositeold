   <a data-wpel-link="external" href="https://github.com/tronprotocol/tips/blob/master/tip-53.md" rel="nofollow external noopener noreferrer" target="_self">
    Github page that hosts the proposal
   </a>
   links back to the official TRON website. Whats more, the proposal is written from the perspective of the TRON Foundation, which openly offered 50 million TRX to the Super Representatives if they agreed to vote for their plan. From the document:
  </p>
  <blockquote>
   <p>
    The proposal will be created on about Oct 15, 2019. Only after the proposal is approved, so can the 50 million TRX distribution be executed.
   </p>
  </blockquote>
  <h2>
   Money Flows Uphill
  </h2>
  <p>
   Bribery or not, that vote went through last October. So why is Justin Sun now making the effort to distance himself from the block reward restructuring?
  </p>
  <p>
   The CEO initially wanted to avoid flack from
   <a data-wpel-link="external" href="https://twitter.com/MustacheTommy/status/1224406499973189632?s=20" rel="nofollow external noopener noreferrer" target="_self">
    TRX holders who were upset at their coins new inflation rate
   </a>
   .
  </p>
  <figure aria-describedby="caption-attachment-231949" class="wp-caption alignnone acss9f89f" id="attachment_231949">

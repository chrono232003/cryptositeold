  <p>
   Suns medium post declares that TRON no longer has a hard cap, and that the increase to block rewards was wholly decentralized:
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad2">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="6634710517" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <blockquote>
   <p>
    TRON rewards were increased to incentivize more people to become SRs and partners and participate in the voting and staking in TRONs network. 90% of the rewards go to the TRX staking holders. All of these functions are decentralized.
   </p>
  </blockquote>
  <p>
   However, a quick glance at
   <a data-wpel-link="external" href="https://tronscan.org/#/sr/representatives" rel="nofollow external noopener noreferrer" target="_self">
    TRONs staking network shows a severe lack of decentralization
   </a>
   . The current Super Representative with the most votes is Binance. Binance controls over 55% of TRONs block production. Over
   <a data-wpel-link="external" href="https://tronscan.org/#/address/TLyqzVGLV1srkB7dToTAEqgDSfPtXRJZYH/voters" rel="nofollow external noopener noreferrer" target="_self">
    98% of Binances votes come from its own cold storage address
   </a>
   .
  </p>
  <h2>
   Billions of Off-the-Books TRX Staked on Binance
  </h2>
  <p>
   A TRON insider told CCN.com it was common knowledge among employees that the company holds several billion off-the-books TRX in Binances staking wallet:
  </p>
  <blockquote>
   <p>
    Quite a few ex-employees mentioned that TRON has a few billion TRX that arent part of the locked up supply that they probably have locked in Binance.
   </p>
  </blockquote>
  <p>
   With 90% of the TRX block reward now being diverted to stakers, it doesnt require a leap to guess who benefits most from the change in block rewards.
  </p>
  <p>
   As it stands, a key component of TRONs whitepaper has been dismissed, and the CEO lied about having nothing to do with it.

     </a>
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     Last week, British telecom giantVodafone became
     <a data-mil="39680" href="https://cryptocurrencynews.com/vodafone-leaves-libra-association/" style="color: #000000;">
      the latest founding member to walk away
     </a>
     from Libra, although the reasons appear more amicable than Mastercards. Vodafone says it will instead focus on its own digital payment service M-Pesa, which it plans to expand beyond the six African nations currently served. However, a Vodafone spokesperson left the door open for a return, saying the company will not rule out the possibility of future cooperation.
    </span>

   <p>
    <span style="color: #000000;">
     Mastercard CEO Ajay Banga says his company left The Libra Association after his attitude towards the project deteriorated over proposals to link the coin with its own built-in wallet, Calibra, as well as the lack of a clear business model.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     <a href="https://business.financialpost.com/technology/blockchain/mastercard-ceo-reveals-why-he-pulled-out-of-facebooks-libra-and-explains-why-nationalization-is-stupid" rel="noopener noreferrer" style="color: #000000;" target="_blank">
      In an interview with the Financial Times,
     </a>
     the payment services chief, who has headed up Mastercard since 2009, described the various red flags that led to his decision to pull the company from the project.
     <em>
      It went from this altruistic idea into their own wallet. Im like: this doesnt sound right,
     </em>
     said Banga, adding that,
     <em>
      If you get paid in Libra [coin]...which go into Calibras, which go back into pounds to buy rice, I dont understand how that works.
     </em>
    </span>

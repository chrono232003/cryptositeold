   <p>
    <span style="color: #000000;">
     He also described the lack of a clear business model as a cause for concern, saying he saw no obvious way in which Libra could become profitable, as well as pointing out fears that the associations members would not commit to anti-money laundering or data management controls. The lack of due diligence over user safety has been a key argument in
     <a data-mil="39680" href="https://cryptocurrencynews.com/libra-association-five-member-committee/" style="color: #000000;">
      regulators opposition to the project
     </a>
     , with Facebooks poor track record of data management and misuse regularly cited as reasons to block the development of Libra.
    </span>
   </p>
   <p>
    <span style="color: #000000;">
     <a data-mil="39680" href="https://cryptocurrencynews.com/facebook-officially-launches-libra-geneva/" style="color: #000000;">
      Mastercard jumped ship from the project in October
     </a>
     , along with PayPal and Visa, just as the official charter to establish the Libra Association, the nonprofit body overseeing the project, was signed in Geneva. The association was originally made up of 28 members; however, the high level of regulatory scrutiny leveled at the project led to several prominent defections, with just 20 members still on board.
    </span>
   </p>

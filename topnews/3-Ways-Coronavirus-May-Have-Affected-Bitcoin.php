   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="6634710517" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <blockquote>
   <p class="p1">
    Bitcoin is not commonly used in China and therefore cannot be a viable replacement for cash. Especially when Wechat and AliPay are already widely accepted in the country.
   </p>
  </blockquote>
  <p>
   Still, on a broader capacity, a switch to digital currencies could reduce the chances of infection.
  </p>
  <p>
   According to a research paper from the Universit de la Mditerrane, laboratory simulations reveal that the superbug
   <a data-wpel-link="external" href="https://www.ncbi.nlm.nih.gov/pubmed/24571076" rel="nofollow external noopener noreferrer" target="_self">
    MRSA can survive on coins.
   </a>
   Meanwhile, the flu, Norovirus, Rhinovirus, hepatitis A and Rotavirus can be transmitted through hand contact.
  </p>
  <p>
   Perhaps a switch to bitcoin isnt such a bad idea
  </p>
  <h2>
   Bitcoin Mining difficulty Declines
  </h2>
  <p>
   One relatively direct consequence of the coronavirus is its bearing upon bitcoin mining.
  </p>
  <p>
   <em>
    <strong>
     Watch: Inside a Secret Chinese Bitcoin Mine.
    </strong>
   </em>
  </p>
  <p>
   <amp-youtube data-videoid="K8kua5B5K3I" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   Chinese authorities have already begun shutting down crypto miners to contain the spread. Earlier this month, Jiang Zhuoer, founder of bitcoin mining pool BTC.Top announced on Weibo that police had forced the
   <a data-wpel-link="external" href="https://www.trustnodes.com/2020/02/06/police-shuts-down-bitcoin-mine-over-coronavirus" rel="nofollow external noopener noreferrer" target="_self">
    mining firm into closure
   </a>
   :
  </p>
  <blockquote>
   <p>
    [translated] Epidemic prevention in some places is already a mess. I have a mine in a remote suburb. The police came to force all the mining authorities and said that they would not resume work.
   </p>
  </blockquote>
  <p>
   Thanks to a mixture of cheap electricity and resources, China dominates as much as
   <a data-wpel-link="exclude" href="https://www.forbes.com/sites/cryptoconfidential/2019/12/15/crypto-ponzi-scheme-china-dominates-bitcoin-mining/#2c6b8528534b" rel="follow noopener" target="_self">
    65% of bitcoin mining.
   </a>
   The loss of mining firms presents the crypto community with a double-edged sword. On one side, bitcoin mining centralization could be reduced, further supporting industry ideals. On the other, network health could be negatively affected due to a lack of miners.
  </p>
  <p>
   Chinas clampdown is seemingly aligned with a reduction in
   <a data-wpel-link="external" href="https://btc.com/stats/diff" rel="nofollow external noopener noreferrer" target="_self">
    mining difficulty growth
   </a>
   . On Feb. 11, the Bitcoin network adjusted a mere 0.52% per data from BTC.com. This comes in stark contrast to Januarys adjustments, which saw network difficulty grow 11.75% in total.
  </p>

     Twitter
    </a>
   </figcaption>
  </figure>
  <p>
   With physical money no longer exchanging hands, the probability of infection is considerably lessened, say proponents.
  </p>
  <p>
   However, bursting the bubble somewhat was Mati Greenspan, who remarks that bitcoins prevalence within China isnt high enough to justify a replacement.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad2">

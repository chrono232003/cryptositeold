   </figcaption>
  </figure>
  <p>
   For many, this is clear-cut evidence to solidify bitcoins status as a risk-off asset. The notion goes that with Chinas
   <a data-wpel-link="internal" href="https://www.ccn.com/coronavirus-massacred-its-economy-but-chinas-still-lying-about-it/" rel="noopener" target="_self">
    economy weakening,
   </a>
   Chinese investors have piled in on bitcoin to make use of its safe-haven narrative.
  </p>
  <p>
   <em>
    <strong>
     Watch: The Coronavirus impact on the Chinese economy.
    </strong>
   </em>
  </p>
  <p>
   <amp-youtube data-videoid="cBAsQLKd44w" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   On Feb. 3, one of Chinas foremost stock indices
   <a data-wpel-link="internal" href="https://www.ccn.com/bloody-monday-chinese-stocks-flash-crash-9-as-coronavirus-wreaks-havoc/" rel="noopener" target="_self">
    the CSI 300plunged 9%
   </a>
   in what was dubbed its worst open in over a decade. To add salt to the already festering wound,
   <a data-wpel-link="exclude" href="https://www.reuters.com/article/us-china-markets/virus-fears-wipe-393-billion-off-chinas-stock-market-despite-government-support-moves-idUSKBN1ZW0TA" rel="noreferrer follow noopener" target="_self">
    the Shanghai Composite Index nosedived 8%
   </a>
   .
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="1136877805" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   Chinese stocks quickly bounced back. An attempt at economic stimulus appeared to do the trick, with the
   <a data-wpel-link="exclude" href="https://www.ft.com/content/6b5ba925-1300-37d9-8f84-52fdb3f6e2f7" rel="follow noopener" target="_self">
    Chinese government cutting interest rates
   </a>
   to bolster the economy. Meanwhile, bitcoin continues to hold near $10,000.
  </p>
  <p>
   For prominent crypto markets analyst Mati Greenspan, the correlation between bitcoin and the coronavirus dip is merely coincidental. Speaking to CCN.com, Greenspan said:
  </p>
  <blockquote>
   <p class="p1">
    Amazingly, Chinese stocks have already fully recovered from the coronavirus dip. If stocks have scant been affected, it would be a difficult case to try and say that crypto has been noticeably moved by this.
   </p>
  </blockquote>
  <h2>
   China Quarantines Infected Cash. Can Bitcoin Fix This?
  </h2>
  <p>
   With the official death toll mounting to 1,775 and 71,811 confirmed
   <a data-wpel-link="external" href="https://gisanddata.maps.arcgis.com/apps/opsdashboard/index.html#/bda7594740fd40299423467b48e9ecf6" rel="nofollow external noopener noreferrer" target="_self">
    cases of the coronavirus
   </a>
   worldwide, China is stepping up its prevention game.
  </p>
  <figure aria-describedby="caption-attachment-234211" class="wp-caption alignnone acss75432" id="attachment_234211">
   <amp-img alt="Coronavirus global cases by Johns Hopkins CSSE CHina" class="wp-image-234211 size-full amp-wp-enforced-sizes" height="660" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/Coronavirus-COVID-19-Global-Cases-by-Johns-Hopkins-CSSE.png" srcset="https://www.ccn.com/wp-content/uploads/2020/02/Coronavirus-COVID-19-Global-Cases-by-Johns-Hopkins-CSSE.png 928w, https://www.ccn.com/wp-content/uploads/2020/02/Coronavirus-COVID-19-Global-Cases-by-Johns-Hopkins-CSSE-300x213.png 300w, https://www.ccn.com/wp-content/uploads/2020/02/Coronavirus-COVID-19-Global-Cases-by-Johns-Hopkins-CSSE-768x546.png 768w" width="928">

  <p>
   As coronavirus continues to spread mass panic around the globe, its impact on bitcoin is becoming more evident. Here are three critical repercussions for BTC amid the outbreak.
  </p>
  <h2>
   As the Coronavirus Spreads, Bitcoin Continues to Break Out
  </h2>
  <p>
   Bitcoin is proving its worth as a macro hedge against global uncertainty. Year-to-date, BTC has achieved a 35% boost and managed to
   <a data-wpel-link="internal" href="https://www.ccn.com/crypto-markets-reach-6-month-milestone-following-monster-rally/" rel="noopener" target="_self">
    hit a yearly high north of $10,000
   </a>
   last week.
  </p>
  <figure aria-describedby="caption-attachment-234201" class="wp-caption alignnone acssb344e" id="attachment_234201">
   <amp-img alt="BTC year to date gains 2020 coronavirus" class="size-full wp-image-234201 amp-wp-enforced-sizes" height="1118" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/BTC-year-to-date-gains-2020-coronavirus-.png" srcset="https://www.ccn.com/wp-content/uploads/2020/02/BTC-year-to-date-gains-2020-coronavirus-.png 1840w, https://www.ccn.com/wp-content/uploads/2020/02/BTC-year-to-date-gains-2020-coronavirus--300x182.png 300w, https://www.ccn.com/wp-content/uploads/2020/02/BTC-year-to-date-gains-2020-coronavirus--1024x622.png 1024w, https://www.ccn.com/wp-content/uploads/2020/02/BTC-year-to-date-gains-2020-coronavirus--768x467.png 768w, https://www.ccn.com/wp-content/uploads/2020/02/BTC-year-to-date-gains-2020-coronavirus--1200x729.png 1200w" width="1840">

     Johns Hopkins CSSE / GIS and Data.
    </a>
   </figcaption>
  </figure>
  <p>
   One of Chinas latest methods to help quell the spread of the coronavirus involves cleansing cash.
  </p>
  <p>
   China has begun using ultraviolet light or
   <a data-wpel-link="exclude" href="https://www.channelnewsasia.com/news/business/china-cleans-banknotes-stop-covid19-spread-12438254" rel="follow noopener" target="_self">
    high temperatures to disinfect banknotes
   </a>
   , according to a central bank press conference. The prevention strategy includes quarantining the banknotes for up to two weeks before redistribution.
  </p>
  <p>
   <amp-youtube data-videoid="Ml3eiXUNKsM" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   Preceding the recent new year celebration, Chinas central bank made an emergency issuance of four billion yuan notes designated for Hubei-the virus epicenter.
  </p>
  <p>
   For the crypto community, this provides another positive narrative as to why cryptocurrencies such as bitcoin are sorely needed.
  </p>
  <figure aria-describedby="caption-attachment-234226" class="wp-caption alignnone acss53e54" id="attachment_234226">
   <amp-img alt="crypto proponents say bitcoin can help stop the spread of the coronavirus" class="size-full wp-image-234226 amp-wp-enforced-sizes" height="812" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/crypto-proponents-say-bitcoin-can-help-stop-the-spread-of-the-coronavirus.png" srcset="https://www.ccn.com/wp-content/uploads/2020/02/crypto-proponents-say-bitcoin-can-help-stop-the-spread-of-the-coronavirus.png 599w, https://www.ccn.com/wp-content/uploads/2020/02/crypto-proponents-say-bitcoin-can-help-stop-the-spread-of-the-coronavirus-221x300.png 221w" width="599">

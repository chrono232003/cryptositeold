  <h2>
   Has Bitcoin Lost Its Disrupt Factor?
  </h2>
  <figure aria-describedby="caption-attachment-230467" class="wp-caption alignnone acss2273f" id="attachment_230467">
   <amp-img alt="bitcoin, btc price" class="size-full wp-image-230467 amp-wp-enforced-sizes" height="667" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961.jpg 1000w, https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961-300x200.jpg 300w, https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961-768x512.jpg 768w, https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961-800x534.jpg 800w" width="1000">
    <amp-img alt="bitcoin, btc price" class="size-full wp-image-230467 amp-wp-enforced-sizes" fallback="" height="667" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg 1000w, https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961-300x200.jpg 300w, https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961-768x512.jpg 768w, https://www.ccn.com/wp-content/uploads/2020/01/shutterstock_648558961-800x534.jpg 800w" width="1000">

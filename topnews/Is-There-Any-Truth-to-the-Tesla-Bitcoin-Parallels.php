    Can bitcoin really be classed as a disruptor? | Image: shutterstock.com
   </figcaption>
  </figure>
  <p>
   And then theres bitcoin.
  </p>
  <p>
   Once touted as the death knell for the traditional financial industry, bitcoins allure as a disruptor has all but dissipated in recent years. Despite being a pioneer of blockchain technology and digital payments, a deficiency in adoption has curbed bitcoins goal of financial dominance.
  </p>
  <p>
   For the most part, Bitcoin losing its disruptor status arises from a trifecta of woes: regulation, innovation and categorization.
  </p>
  <p>
   <span class="acss7a230">
    A lack of cohesive regulation has left BTC adoption staggered
   </span>
   <span class="acss7a230">
    across multiple jurisdictions. In the U.S., while
    <a data-wpel-link="internal" href="https://www.ccn.com/as-sec-debates-whether-cryptocurrencies-are-securities-initiative-seeks-to-distance-xrp-from-ripple/" rel="noopener noreferrer" target="_self">
     the SEC continues to debate over security classifications of specific cryptocurrencies
    </a>
    , the IRS defines bitcoin as 
    <a data-wpel-link="exclude" href="https://www.irs.gov/pub/irs-drop/n-14-21.pdf" rel="noreferrer follow noopener" target="_self">
     property for U.S. federal tax purposes,
    </a>
    and
    <a data-wpel-link="exclude" href="https://www.cftc.gov/PressRoom/PressReleases/7820-18" rel="noreferrer follow noopener" target="_self">
     the CFTC considers digital assets commodities.
    </a>
    All of which contribute to the confusion.
   </span>
  </p>
  <p>
   <span class="acss7a230">
    Constant disagreement as to its use case has similarly buckled bitcoinwith no real consensus as to whether its
    <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-best-store-of-value-over-last-decade/" rel="noopener" target="_self">
     a store of value
    </a>
    ,
    <a data-wpel-link="internal" href="https://www.ccn.com/square-crypto-is-paving-the-way-for-bitcoin-to-muscle-cash-out-of-the-way/" rel="noopener" target="_self">
     a medium of exchange
    </a>
    , or both. As a consequence, the markets have become saturated. The cryptocurrency markets are now chock-full of bitcoin derivatives, each touting a sounder use-case than the lastproviding further dilution in bitcoins mission toward disruption.
   </span>
  </p>
  <h2>
   Blockchain, Not Bitcoin
  </h2>
  <p>
   <span class="acss7a230">
    Perhaps most damning of all is the argument that blockchain is a better disruptor than bitcoin.
   </span>
  </p>
  <p>
   <amp-youtube data-videoid="ahWcUZfSufI" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   <span class="acss7a230">
    
   </span>
   <span class="acss7a230">
    Bitcoin, not blockchain is an adage typically used by non-believers.
    <a data-wpel-link="internal" href="https://www.ccn.com/trumps-former-economic-advisor-joins-the-blockchain-not-bitcoin-brigade/" rel="noopener" target="_self">
     From Goldman Sachs President Gary Cohn
    </a>
    to U.S.
    <a data-wpel-link="internal" href="https://www.ccn.com/us-militarys-darpa-program-hops-on-the-blockchain-not-bitcoin-bandwagon/" rel="noopener" target="_self">
     defense agency DARPA
    </a>
    , those who rank BTC lower than its underlying tech appear to be winning the debate.
   </span>
  </p>
  <p>
   Last April,
   <a data-wpel-link="exclude" href="https://www.forbes.com/sites/michaeldelcastillo/2019/04/16/blockchain-50-billion-dollar-babies/#24a3007d57cc" rel="follow noopener" target="_self">
    Forbes released its Blockchain 50 lista round-up for billion-dollar companies employing blockchain tech
   </a>
   . The list included big shots such as Amazon Web Services, BBVA (Spains second-largest bank), Citigroup and, of course, Facebook. All of which tend to turn a blind eye toward bitcoin itself.
  </p>
  <p>
   At the end of it, despite what analysts say, bitcoins relationship with Tesla is negligible at best, and downright ridiculous at worst.
  </p>

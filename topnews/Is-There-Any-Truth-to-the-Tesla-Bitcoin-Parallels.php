  <p>
   Thanks to a significant uptick in Tesla (NASDAQ:TSLA) stock since the start of 2020, a new narrative has emerged: Tesla stock is
   <em>
    apparently
   </em>
   the new bitcoin. But is there any truth to this beyond a trivial analytic link?
  </p>
  <p>
   <a data-wpel-link="internal" href="https://www.ccn.com/tesla-soars-to-725-as-bears-get-blown-out/" rel="noopener" target="_self">
    The chronicle of Teslas price surge
   </a>
   and its kinship to BTCis being picked apart by nearly every analyst of late. One of the most recent speculators to highlight the quasi-relationship is Bloomberg Intelligence analyst Mike McGlone.
  </p>
  <p>
   <amp-youtube data-videoid="roaTn2ehPE8" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   <a data-wpel-link="external" href="https://www.youtube.com/watch?v=roaTn2ehPE8" rel="nofollow external noopener noreferrer" target="_self">
    Speaking on an episode of Charting Futures,
   </a>
   McGlone observed that Teslas stock price seems to be mimicking its 2013 performancea year that witnessed TSLA explode from $32 to $190 in the space of nine months:
  </p>
  <blockquote>
   <p>
    On the long-term chart, [TSLA] trades just like it did in 2013, which was one of the biggest years ever. And by the way, that was the biggest year ever for Bitcoin and it looks to me like youre seeing a little bit of Bitcoin catching up.
   </p>
  </blockquote>

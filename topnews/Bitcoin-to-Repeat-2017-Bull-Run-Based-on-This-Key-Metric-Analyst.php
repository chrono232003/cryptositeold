  <p>
   In October 2018, its possible that the net growth of entities spiked because many were hoping that $6,000 would be the bitcoin bear market bottom. This time its different because the
   <a data-wpel-link="internal" href="https://www.ccn.com/beautiful-trendline-bitcoin-explode-travis-kling/" rel="noopener" target="_self">
    top cryptocurrency looks awfully strong
   </a>
   .
  </p>
  <p>
   Fundstrats Thomas Lee shared on Twitter that bitcoin is trading above its 200-day moving average. This is essentially a signal that the coin is entering bull territory.

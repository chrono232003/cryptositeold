  <p>
   Bitcoin bulls have a lot of reasons to be excited. On Wednesday, the top cryptocurrency posted a fresh 2020 high of $9,443.96. Its also up
   <a data-wpel-link="internal" href="https://www.ccn.com/safe-haven-bitcoin-rockets-toward-3-month-high-up-32-ytd/" rel="noopener" target="_self">
    over 30% year-to-date
   </a>
   and it continues to show signs of strength.
  </p>
  <p>
   The top cryptocurrency is close to breaching the resistance of a key metric. Im talking about the number of entities entering bitcoin on a daily basis. The growth of entities strongly suggests that more people are using the cryptocurrency. This metric is relevant because the last time adoption grew, the cryptocurrency launched a bull market.
  </p>
  <h2>
   Number of Bitcoin Holders Vs. Net Growth of Entities
  </h2>
  <p>
   Getting the actual number of bitcoin holders or users is a herculean task. Thats because there are two obstacles:
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="1136877805" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <ul>
   <li>
    One holder can have multiple wallet addresses;
   </li>
   <li>
    One bitcoin address, such one belonging to as a crypto exchange, can hold the funds of multiple individuals.
   </li>
  </ul>
  <p>
   To get a good gauge of widespread adoption, blockchain intelligence firm
   <a data-wpel-link="external" href="https://medium.com/glassnode-insights/how-many-entities-hold-bitcoin-e945ecc5d0a1" rel="nofollow external noopener noreferrer" target="_self">
    Glassnodes mapped the number of entities controlling multiple addresses
   </a>
   . For now, this is the closest you can get to approximating the number of bitcoin holders. According to Glassnodes,
  </p>
  <blockquote>
   <p>
    This work is the first to introduce a more sophisticated quantification of Bitcoin entities that goes beyond plain address counts and oversimplified heuristics.
   </p>
  </blockquote>
  <h2>
   Spike in Net Growth of Entities Preceded the 2017 Bull Run
  </h2>
  <p>
   With that in mind, Glassnodes charted the daily net growth of entities. Again, this is a metric that more closely measures how many users are using bitcoin. Thats because its mapping multiple addresses to a single entity. In addition, this metric takes into consideration entities with zero BTC balance but had a non-zero BTC balance before. Its not surprising to see that bitcoins price rose while the daily net growth of entities increased.
  </p>

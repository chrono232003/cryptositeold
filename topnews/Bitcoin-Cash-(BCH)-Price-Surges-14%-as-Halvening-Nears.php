  <p>
   One positive outlook for bitcoin cash may involve the coins halvening, which is
   <a data-wpel-link="external" href="https://coinsalad.com/bitcoincash/halving" rel="nofollow external noopener noreferrer" target="_self">
    scheduled for Apr. 8
   </a>
   . Thats just over a month
   <i>
    before
   </i>
   bitcoins (BTC) own halving, and may help explain why BCH has outperformed many of its peers of late.
  </p>
  <p>
   An analogous situation can be seen with bitcoin SV, which is also due to halve its block rewards in April. The coin led by the
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-svs-craig-wright-bites-back-kleiman-lawyers/" rel="noopener" target="_self">
    self-proclaimed Satoshi Nakamoto, Craig Wright
   </a>
   , gained 456% from Decembers low. After a significant pullback, the coin retains 254% growth over the past nine weeks.
  </p>

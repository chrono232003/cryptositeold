  <h2>
   Shrugs Off Tax Man; Halvening In Sight
  </h2>
  <p>
   While bitcoin cash benefited most from the markets rising tide on Wednesday, observers still had cause to be surprised. The coins community has been
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-cash-community-fractured-by-sudden-12-5-mining-tax/" rel="noopener" target="_self">
    locked in heated debate for nearly two weeks
   </a>
   concerning an internal development fund.
  </p>
  <p>
   The BCH tax fund was initially deemed an imposition by a large section of its community. After
   <a data-wpel-link="internal" href="https://www.ccn.com/hash-war-looms-as-bitcoin-cash-tax-drama-explodes/" rel="noopener" target="_self">
    an anonymous group of miners threatened to hard fork away
   </a>
   from the taxed chain, the tax plan was apparently
   <a data-wpel-link="internal" href="https://www.ccn.com/crypto-killed-the-tax-man-bitcoin-cash-escapes-hash-war-over-mining-tax-grenade/" rel="noopener" target="_self">
    pulled back to the discussion stage
   </a>
   .
  </p>

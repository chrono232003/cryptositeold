        <section class="article-body has-media">
 <div class="article-pharagraph" id="node-0">
  <p class="text">
   The decision by India's Supreme Court to lift the central banks ban on cryptocurrency trading could soon translate into notable growth in trading volumes, according to cryptocurrency exchanges in the country.
  </p>
 </div>
 <div class="article-pharagraph" id="node-1">
  <p class="text">
   Indias Supreme Court on Wednesday quashed a Reserve Bank of India (RBI) order dated April 6, 2018, which prohibited banks from providing services to entities dealing with cryptocurrencies.
  </p>
 </div>
 <div class="article-pharagraph" id="node-2">
  <p class="text">
   The top court called RBIs ban unconstitutional, bringing cheer to the crypto market community.
  </p>
 </div>
 <div class="article-pharagraph" id="node-3">
  <p class="text">
   Put simply, Indian traders will now be able to directly deposit Indian rupee (INR) from bank accounts to crypto exchanges. As a result, it will be more convenient for users to cash in and cash out of their holdings.
  </p>
 </div>
 <div class="article-pharagraph" id="node-4">
  <p class="text">
   This is the first step towards embracing cryptocurrency in India, which has the potential of becoming one of the largest crypto markets. Ashish Singhal, chief executive of the cryptocurrency exchange CoinSwitch.co, told CoinDesk.
  </p>
 </div>
 <div class="article-pharagraph" id="node-5">
  <p class="text">
   India was doing very well in terms of trading volumes, contributing about $50 million to $60 million per day before the RBI ban, according to Singhal.
  </p>
 </div>
 <div class="article-pharagraph" id="node-6">
  <p class="text">
   In India, a huge damage was done due to the lack of awareness and RBIs decision, said Kumar Gaurav, founder and CEO at online crypto banking platform Cashaa.
  </p>
 </div>
 <div class="article-pharagraph" id="node-7">
  <p class="text">
   Volumes subsequently dipped after the central bank
   <a data-reactroot="" href="/rbi-bars-banks-business-crypto-firms">
    issued banking restrictions
   </a>
   and commercial banks responded by advising account holders not to engage in cryptocurrency transactions.
  </p>
 </div>
 <div class="article-pharagraph" id="node-8">
  <p class="text">
   For instance, Kotak Mahindra Bank, one of the largest lenders in India, has diligently sent multiple notification emails to account holders in the last two years warning against the use of credit cards for cryptocurrency exchanges.
  </p>
 </div>
 <div class="article-pharagraph" id="node-9">
  <p class="text">
   In line with the instructions issued by RBI, we also wish to advise you that, if transactions relating to any kind of virtual currencies are witnessed on your Kotak Credit Card, the Bank shall be constrained to block your Credit Card without any further intimation, the email reads.
  </p>
 </div>
 <div class="article-pharagraph" id="node-10">
  <p class="text">
   As a result, Indians crypto traders were forced to use peer-to-peer crypto trading platforms, which allow direct transfer of cryptocurrencies into the individual accounts without the intervention of any financial institution or government authority. However, liquidating cryptocurrency holdings is quite difficult when dealing with P2P platforms.
  </p>
 </div>
 <div class="article-pharagraph" id="node-11">
  <p class="text">
   Also, RBIs decision was widely misinterpreted as legal ruling deeming cryptocurrency trading as an unlawful activity.
  </p>
 </div>
 <div class="article-pharagraph" id="node-12">
  <p class="text">
   With the Supreme Courts latest ruling, the situation is widely expected to change for the good.
  </p>
 </div>
 <div class="article-pharagraph" id="node-13">
  <p class="text">
   Crypto exchanges including WazirX will now be able to enable banking channels for fiat deposit and withdrawals, said Nischal Shetty, founder &amp; CEO of Mumbai-based cryptocurrency exchange WazirX, which was recently
   <a data-reactroot="" href="/binance-enters-indian-market-with-acquisition-of-crypto-exchange-wazirx">
    acquired by Binance
   </a>
   , the world's largest exchange by trading volume.
  </p>
 </div>
 <div class="article-pharagraph" id="node-14">
  <p class="text">
   Shetty expects volumes on Indian cryptocurrency exchanges to grow by 10 times in the near future.
  </p>
 </div>
 <div class="article-pharagraph" id="node-15">
  <p class="text">
   CoinSwitchs Ashish Singhal anticipates average daily volume rising as high as $50 million to $60 million  the level seen before the RBI ban  and may surpass that level.
  </p>
 </div>
 <div class="article-pharagraph" id="node-16">
  <p class="text">
   A sharp rise in volumes cannot be ruled out in India, a country with a population of over 1 billion. The Supreme Court judgment could help erase the misconception that cryptocurrencies are illegal and may draw more investors to the market.
  </p>
 </div>
 <div class="article-pharagraph" id="node-17">
  <p class="text">
   The clarity that the judgment has brought, will help crypto adoption as a whole and which in tune will see a spike in volume, said CoinDCX Chief Executive Sumit Gupta. CoinDCX has actively voiced its thoughts on behalf of Indias crypto community on the RBI ban over the last two years and was
   <a data-reactroot="" href="/indian-crypto-exchange-adds-bank-transfers-hours-after-rbi-ban-lifted">
    quick to allow bank transfers
   </a>
   following the ruling.
  </p>
 </div>
 <div class="article-heading" id="node-18">
  <h4>
   <strong>
    Focus on compliance
   </strong>
  </h4>
 </div>
 <div class="article-pharagraph" id="node-19">
  <p class="text">
   The Supreme Court decision to lift the bank is a forthcoming step and cryptocurrency exchanges should now start focusing on deploying stronger know-your-client (KYC), user data privacy, and AML policies, according to Arpit Ratan, co-founder of RegTech startup Signzy.
  </p>
 </div>
 <div class="article-pharagraph" id="node-20">
  <p class="text">
   Indeed, that would help build trust and reduce risks of cryptocurrency transactions being exploited for illegal activities, such as crimes, money laundering and tax evasions.
  </p>
 </div>
 <div class="article-pharagraph" id="node-21">
  <p class="text">
   Moreover, Indian lenders serving cryptocurrency entities will now face similar which banks in other, developed countries such as Japan, Europe, and the U.S. are facing.
  </p>
 </div>
 <div class="article-pharagraph" id="node-22">
  <p class="text">
   Cashaa, with its huge crypto-focused customer base, can provide a powerful system that will be very helpful for RBI to understand the [anti-money laundering] issues coming due to onboarding crypto companies is needed, said Kumar Gaurav, founder and CEO of Cashaa.
  </p>
 </div>
 <div class="article-heading" id="node-23">
  <h4>
   <strong>
    Premature optimism?
   </strong>
  </h4>
 </div>
 <div class="article-pharagraph" id="node-24">
  <p class="text">
   The general consensus is the Supreme Courts ruling would open a path to favorable regulations towards protecting all stakeholders, including companies, customers and users.
  </p>
 </div>
 <div class="article-pharagraph" id="node-25">
  <p class="text">
   However, despite the top courts decision, the government could still ban cryptocurrencies.
  </p>
 </div>
 <div class="article-pharagraph" id="node-26">
  <p class="text">
   Theres still a bill that the Parliament has to discuss. It has changed shapes and forms but hopefully this will be the framework for how cryptocurrency is traded in the country," said Prashant Swaminathan, founder and CEO of Tandem, a peer-to-peer
   <a aria-label="bitcoin (opens in a new tab)" data-reactroot="" href="/price/bitcoin" rel="noreferrer noopener" target="_blank">
    bitcoin
   </a>
   (BTC) trading platform told Mint, an Indian financial daily newspaper.
  </p>
 </div>
 <div class="article-pharagraph" id="node-27">
  <p class="text">
   The Indian government had submitted a draft crypto bill named entitled Banning of Cryptocurrency &amp; Regulation of Official Digital Currencies to the Supreme Court in August 2019, seeking to ban the use of cryptocurrency as legal tender or currency and prohibit mining, buying, holding, selling, dealing in, issuance, disposal or use of cryptocurrency.
  </p>
 </div>
 <div class="article-pharagraph" id="node-28">
  <p class="text">
   However, the government did not introduce the draft bill in the winter session of the parliament held between Nov. 18 and Dec. 13.
  </p>
 </div>
 <div class="article-pharagraph" id="node-29">
  <p class="text">
   So the regulations and legalities of operating cryptocurrency businesses in India remain unclear. As a result, volume growth may still not be as strong as some expect.
  </p>
 </div>

  <p>
   A video from 2013 depicting a European ministers tirade against the global banking system has recently re-emerged and gone viral. Gaining massive traction within the crypto community, the speech is now more relevant than ever  and central banks know it.
  </p>
  <h2>
   All the Banks Are Broke
  </h2>
  <p>
   Within an impassioned speech, British politician and former member of European Parliament (MEP) Godfrey Bloom thoroughly dismantles the global banking sector, proposing that banks are broke and highlighting what he calls institutional incompetence and chicanery.
  </p>
  <p>
   <em>
    <strong>
     Watch: Godfrey Blooms tirade against the financial sector
    </strong>
   </em>
  </p>
  <p>
   <amp-youtube data-videoid="hYzX3YZoMrs" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   Packing in as much vitriol as one can within a two minutes, Bloom systematically tears the financial system apart.
  </p>
  <p>
   Aiming at the sectors penchant for fractional reserve banking, the former-MEP remarks that banks lending money that they dont actually have is tantamount to a criminal scandal.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad1">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="1136877805" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   Financial institutions around the world have been operating fractional reserve systems for many years. The practice entails using the vast majority of customer deposits to generate profit via interest rates on loans. This results in a mere fraction of bank deposits (10% for larger institutions) remaining available for withdrawal.
  </p>
  <p>
   Bloom also focused on the moral hazard within banking, alluding that the governments promise to bail out banksas they have done time and time againencourages riskier behavior.
  </p>
  <figure aria-describedby="caption-attachment-233906" class="wp-caption alignnone acssb4ca0" id="attachment_233906">
   <amp-img alt="The now-viral video of Bloom berating the banking secto" class="size-full wp-image-233906 amp-wp-enforced-sizes" height="379" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/Screen-Shot-2020-02-14-at-18.39.59.png" srcset="https://www.ccn.com/wp-content/uploads/2020/02/Screen-Shot-2020-02-14-at-18.39.59.png 595w, https://www.ccn.com/wp-content/uploads/2020/02/Screen-Shot-2020-02-14-at-18.39.59-300x191.png 300w" width="595">

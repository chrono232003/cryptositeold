  <p>
   Most pertinent of all was Blooms take on quantitative easing (QE):
  </p>
  <blockquote>
   <p>
    We have counterfeiting, sometimes called quantitative easing, but counterfeiting by any other namethe artificial printing of moneywhich, if any ordinary person did, theyd go to prison for a very long time, and yet governments and central banks do it all the time.
   </p>
  </blockquote>
  <h2>
   Quantitative Easing Cant Last Forever
  </h2>
  <p>
   In the decade since the financial crisis, the worlds central banks have combated rising interest rates by buying copious quantities of government debtaka QE.
  </p>
  <p>
   <em>
    <strong>
     Watch: Trump calls for negative interest rates.
    </strong>
   </em>
  </p>
  <p>
   <amp-youtube data-videoid="5vyMt2DSV9U" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   In 2019 following a dovish turn from the U.S. Federal Reserve, global economies started upping their fiscal policy games. Among the more unconventional monetary maneuvers was QE.
  </p>
  <p>
   In 2019, the
   <a data-wpel-link="internal" href="https://www.ccn.com/the-fed-just-blew-the-lid-on-the-next-financial-crisis/" rel="noopener" target="_self">
    Feds repo operations offered a veneered return to QE
   </a>
   . Meanwhile, the European Central Bank (ECB) didnt even bother with the charade, announcing in September that it would start up QE once again,
   <a data-wpel-link="exclude" href="https://www.forbes.com/sites/isabeltogoh/2019/09/12/what-to-expect-from-the-european-central-bank-decision/#38a4ab953df9" rel="follow noopener" target="_self">
    buying up bonds
   </a>
   at a rate of 20 billion for as long as necessary.
  </p>
  <p>
   While QE proved to quell the last financial crisis, the artificial printing of money  to quote Bloom  isnt sustainable.
  </p>
  <div class="amp-ad-wrapper amp_ad_1 ampforwp-incontent-custom-banner ampforwp-incontent-ad2">
   <span>
    Advertisement
   </span>
   <amp-ad data-ad-client="ca-pub-6472069693657143" data-ad-slot="6634710517" data-auto-format="rspv" data-full-width="" height="320" type="adsense" width="100vw">
    <div overflow="">
    </div>
   </amp-ad>
  </div>
  <p>
   Worryingly, speaking before the Senate Banking Committee last week, Federal Reserve Chairman Jerome Powell commented that the Fed
   <a data-wpel-link="internal" href="https://www.ccn.com/fed-chair-jerome-powell-just-admitted-the-u-s-economy-is-screwed/" rel="noopener" target="_self">
    would use QE
   </a>
   aggressively should another recession hit.
  </p>
  <blockquote>
   <p>
    It is much more likely we will have to turn to the tools we used in the financial crisis [] I believe we will use them aggressively should the need arise to do so. There is no need to do that now but we will use those tools aggressively.
   </p>
  </blockquote>
  <p>
   <em>
    <strong>
     Watch: Jerome Powell tells lawmakers a return to Q.E. is on the cards
    </strong>
   </em>
  </p>
  <p>
   <amp-youtube data-videoid="jRKT72K2RCw" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   The problem is, many economistsas well as the World Economic Forum (WEF)have noted that QE may not be as effective as it was last time. According to its 2019 annual
   <a data-wpel-link="external" href="https://www3.weforum.org/docs/WEF_TheGlobalCompetitivenessReport2019.pdf" rel="noreferrer external nofollow noopener" target="_self">
    Global Competitiveness Report
   </a>
   , the WEF opines that unlike 2008,
   <a data-wpel-link="internal" href="https://www.ccn.com/the-next-global-recession-will-have-no-brakes-claims-world-economic-forum/" rel="noopener" target="_self">
    the next recession
   </a>
   will leave central banks with fewer policy options.
  </p>
  <h2>
   The Banks Know Their Time Is up
  </h2>
  <figure aria-describedby="caption-attachment-174626" class="wp-caption alignnone acss2273f" id="attachment_174626">
   <amp-img alt="crypto replace failing dollar" class="size-full wp-image-174626 amp-wp-enforced-sizes" height="750" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2019/04/burn-dollar-fiat-crypto-shutterstock.jpg" srcset="https://www.ccn.com/wp-content/uploads/2019/04/burn-dollar-fiat-crypto-shutterstock.jpg 1000w, https://www.ccn.com/wp-content/uploads/2019/04/burn-dollar-fiat-crypto-shutterstock-300x225.jpg 300w, https://www.ccn.com/wp-content/uploads/2019/04/burn-dollar-fiat-crypto-shutterstock-768x576.jpg 768w" width="1000">
    <amp-img alt="crypto replace failing dollar" class="size-full wp-image-174626 amp-wp-enforced-sizes" fallback="" height="750" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg" srcset="https://www.ccn.com/wp-content/uploads/2020/01/stock-market-fed-not-qe-afp.jpg 1000w, https://www.ccn.com/wp-content/uploads/2019/04/burn-dollar-fiat-crypto-shutterstock-300x225.jpg 300w, https://www.ccn.com/wp-content/uploads/2019/04/burn-dollar-fiat-crypto-shutterstock-768x576.jpg 768w" width="1000">
    </amp-img>
   </amp-img>
   <figcaption class="wp-caption-text" id="caption-attachment-174626">
    QE cant last forever. | Source: Shutterstock
   </figcaption>
  </figure>
  <p>
   The cryptocurrency industryunderlined by blockchain techis slowly reaching critical mass. Bitcoin adoption is
   <a data-wpel-link="external" href="https://medium.com/glassnode-insights/how-many-entities-hold-bitcoin-e945ecc5d0a1" rel="nofollow external noopener noreferrer" target="_self">
    on an exponential rise,
   </a>
   while companies ranging from Amazon to
   <a data-wpel-link="exclude" href="https://www.forbes.com/sites/michaeldelcastillo/2018/07/03/big-blockchain-the-50-largest-public-companies-exploring-blockchain/" rel="follow noopener" target="_self">
    Alibaba, are adopting blockchain.
   </a>
   Now, central banks are starting to sweat.
  </p>
  <p>
   To keep the threat of crypto at bay, many institutions accept that its time to overhaul the antiquated financial system via central bank digital currencies (CBDCs).
  </p>
  <p>
   <amp-youtube data-videoid="rpJbEpWPaTI" height="563" layout="responsive" width="1000">
   </amp-youtube>
  </p>
  <p>
   From the
   <a data-wpel-link="exclude" href="https://www.bankofengland.co.uk/news/2020/january/central-banks-group-to-assess-digital-currencies" rel="follow noopener" target="_self">
    Bank of England-led research
   </a>
   collective to the Peoples Bank of Chinas incipient CBDC, central banks all over the globe are hurrying to either implement a digital currency or actively research them.
  </p>
  <p>
   The topic of CBDCs was even broached by Powell
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-price-soars-jerome-powell-confirms-cryptos-threat-to-dollar/" rel="noopener" target="_self">
    during a congressional hearing
   </a>
   last week.
  </p>
  <blockquote>
   <p>
    Every major central bank is currently taking a deep look, Powell said, We feel thats our obligation, technology has now made that possible. I think its very much incumbent on us and other central banks to understand the costs and benefits and tradeoffs associated with a possible digital currency.
   </p>
  </blockquote>
  <p>
   The pressure to compete with China, combined with private sector initiatives such as bitcoin and Facebooks Libra, its do or die time for the U.S. and Wall Street.
  </p>

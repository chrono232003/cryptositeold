  <p>
   In this environment, investors will turn to other assets to generate returns. Bitcoin is a prime candidate. Alex Kruger shares the same view. When asked if the top cryptocurrency will thrive in a deflationary environment, the economist responded,
  </p>
  <blockquote>
   <p>
    Yes, same as gold. Gold benefits from periods of hyperinflation or negative real yields. Bitcoin should be the same in this regard.
   </p>
  </blockquote>
  <p>
   Hans Hauge, analyst at Ikigai fund, has the same sentiment. He told CCN,
  </p>
  <blockquote>
   <p>
    I think this is where we see if bitcoin can move from being a risk asset to a safe haven. Right now, bitcoin is benefiting from the massive asset bubble that we see in private equity, the stock market, venture capital, etc. But bitcoin is also spreading roots into the global financial system. Its like an insurance policy that just keeps getting bigger.
   </p>
  </blockquote>
  <p>
   In other words, the Fed will be busy propping assets while devaluing the U.S. dollar and generating low to negative yields. This will drive investors to seek alternatives that are proven to be scarce. Bitcoin is on
   <a data-wpel-link="internal" href="https://www.ccn.com/top-german-bank-predicts-bitcoin-to-hit-90000-after-halving/" rel="noopener" target="_self">
    course to become ultrahard money
   </a>
   , harder than gold.
  </p>
  <p>
   It appears that the Fed is creating the rocket fuel that may send bitcoin to the moon.
  </p>

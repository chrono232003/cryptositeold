  <p>
   The Fed is flooding the financial system with hundreds of billions of dollars yet the consumer price index (CPI) is not overheating.
  </p>
  <p>
   How is that possible?
  </p>
  <p>
   The Federal Reserve is doing a marvelous job of keeping the money out of the hands of ordinary people. Theyre giving it to banks and hedge funds who are either hoarding cash or
   <a data-wpel-link="internal" href="https://www.ccn.com/jp-morgan-caused-repo-market-stress-so-the-bank-can-buy-back-shares-analyst/" rel="noopener" target="_self">
    betting it on the stock market
   </a>
   . Brendan Bernstein, founding partner at crypto hedge fund Tetras Capital, adds that many are using the money to service debt rather than buy goods and services.
  </p>

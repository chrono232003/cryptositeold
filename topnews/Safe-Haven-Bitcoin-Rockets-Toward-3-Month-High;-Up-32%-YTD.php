  <p>
   While bitcoin still has a long way to go before its ever considered a bona fide safe haven, its correlation with gold is growing stronger.
  </p>
  <p>
   For the first time this year,
   <a data-wpel-link="internal" href="https://www.ccn.com/bitcoin-beats-gold-in-safe-haven-battle-coronavirus-death-toll-rises/" rel="noopener" target="_self">
    bitcoins 60-day correlation with the yellow metal
   </a>
   has turned positive, according to Bloomberg. Put differently, bitcoin has been acting more like gold and less like stocks in recent months.
  </p>

  <h2>
   BTC/USD Rallies; Altcoins Follow
  </h2>
  <p>
   Bitcoin peaked at $9,429.99 on Bitstamp, its highest since early November. The flagship cryptocurrency was last seen trading at $9,326, having gained more than 3%.
  </p>
  <p>
   The technical charts showcase an asset thats in a clear uptrend. The latest candle on the daily chart puts bitcoin above the 200-day moving average. It blew past the 50-day MA more than three weeks ago.
  </p>

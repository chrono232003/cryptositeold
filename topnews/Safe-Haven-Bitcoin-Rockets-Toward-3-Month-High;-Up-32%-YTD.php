  <p>
   At current values, bitcoin accounts for 66.4% of the overall cryptocurrency market. Its market cap of nearly $170 billion represents a $50 billion recovery from last months swing low.
  </p>
  <p>
   Altcoins and tokens followed in bitcoins lead on Wednesday, as Ethereum, bitcoin cash, Litecoin and EOS all reported gains of at least 2%. The total crypto market cap peaked near $258 billion, the highest since late September.
  </p>
  <h2>
   Battle of the Safe Havens
  </h2>
  <p>
   Bitcoin is not only the best-performing asset of the last decade, its arguably the most attractive haven for investors looking to escape economic and geopolitical risks.
  </p>
  <p>
   On Tuesday, Fundstrats Tom Lee compared bitcoins performance to gold in the so-called battle of the safe havens. At the time, bitcoin had gained 26% year-to-date. At the highest point on Wednesday, the year-to-date returns were roughly 32%.
  </p>
  <p>
   By comparison, gold is up a decent 3% on the year.
  </p>

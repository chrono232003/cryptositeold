  <h2>
   Search Interest for Bitcoin Halving Is Accelerating
  </h2>
  <p>
   The correlation between cryptocurrency investor attention and bitcoin price action has been well-documented. A study showed that
   <a data-wpel-link="internal" href="https://www.ccn.com/google-trends-bitcoin-searches-bullish/" rel="noopener" target="_self">
    Google Trends data as a proxy for investor attention is a strong predictor of bitcoins performance
   </a>
   . The explosion of the search term bitcoin halving indicates that many may just be beginning to learn the concept.
  </p>
  <figure aria-describedby="caption-attachment-234228" class="wp-caption aligncenter acssf2f90" id="attachment_234228">
   <amp-img alt="The search term bitcoin halving is on a strong uptrend since December 2019" class="size-full wp-image-234228 amp-wp-enforced-sizes" height="616" layout="intrinsic" src="https://www.ccn.com/wp-content/uploads/2020/02/halving.png" srcset="https://www.ccn.com/wp-content/uploads/2020/02/halving.png 1156w, https://www.ccn.com/wp-content/uploads/2020/02/halving-300x160.png 300w, https://www.ccn.com/wp-content/uploads/2020/02/halving-1024x546.png 1024w, https://www.ccn.com/wp-content/uploads/2020/02/halving-768x409.png 768w" width="1156">
